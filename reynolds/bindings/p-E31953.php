<P CLASS=HeadC>Le Surm&#0226;le <BR>
(Superman)</P> 
<BR>
<IMG SRC="/reynolds/images/E31953.jpg" WIDTH="350" HEIGHT="390" BORDER="1" ALT=""><BR>
<BR>


<P>Alfred Jarry, best known for his works that pioneered the Theatre of the Absurd, wrote the first (1902) significant, modern tale of "superman." The account is framed around the story of Messalina, third wife of the Roman Emperor Claudius, known for her prodigious sexual endurance. Jarry imagined a male counterpart who, after shattering a world's endurance record, is paired up with the ultimate aphrodisiac: a machine created to inspire love and provide supreme satisfaction. The superman's power, greater than that of the machine, inspires such passion that the mechanical being becomes white-hot, too much for flesh and blood to bear: superman dies.</P>
<P>Mary Reynolds's binding alludes both to the romantic aspect of love, with the image of the butterfly wrapping the book, and to the erotic love machine, with the metal corset stay on the spine arising from the delicate, defenseless butterfly like an erection bursting forth with lust, desire, and the need to dominate. The mauve-colored endpapers printed in a wire mesh pattern extend further still the theme of mechanical dominance. Even the near monotone palette speaks of the control of the mechanics of sex over intimate tenderness. Overtones of the potential self-destructiveness of unbridled passion are omnipresent. </P><BR>
