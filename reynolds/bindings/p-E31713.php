<P>The toad skins laid on both the front and back covers are an obvious reference to the subject of evolution found in the title of <I>La Science de dieu.</I>  In true Surrealist fashion, however, the book is not what it appears to be. Brisset was not referring to physical evolution, but to the evolution of language.</P>
<P>The genus of toad that Reynolds used is <I>Bufo,</I> a common variety with worldwide distribution.  Reynolds used a variety of alluring and unexpected skins, including ostrich and boa constrictor, which were not difficult to obtain in the Paris of the 1920s and 1930s. In these toads, the flaps of skin behind each eye are glands called parotids that secrete poison.</P><BR>
