<P CLASS=HeadC>Gestes et opinions du Docteur Faustroll, pataphysicien <BR>(The Exploits and Opinions of Doctor Faustroll, Pataphysician)</P><BR>
<IMG SRC="/reynolds/images/E31952.jpg" WIDTH="325" HEIGHT="390" BORDER="1" ALT=""><BR>
<BR>

<P>Reynolds created this binding based on Duchamp's windows, <I>Fresh Widow</I> (1920) and <I>Brawl at Austerlitz</I> (1921). In 1936 Duchamp revisited his windows and had the <i>Brawl</i> printed on paper and celluloid for Andr&eacute; Breton's <I>Au Lavoir noir</I> (Paris: Editions GLM, 1936). It was enclosed loose in each of the seventy copies. Presumably, Reynolds revisited this concept with him with this inspired fen&#0234;tre binding as a result.</P>
<P>Duchamp created his windows with opaque panes that could not be seen through: functional objects that did not function, another of his plastic puns. Unlike <i>Fresh Widow,</I> the window panes on this binding are not completely darkened, but dotted with uneven holes, inviting the viewer to look in, but allowing only a glimpse of the contents.</P>
<P>"Pataphysics," a concept Jarry (1873&#150;1907) introduced in this book, is the study of a realm additional to metaphysics, a science of imaginary solutions. Pataphysics welcomes all scientific explanations for the universe, suspending all values moral, aesthetic, and otherwise. Jarry regarded reality as a cosmic joke.</P><BR>
