<P CLASS=HeadC>Le Vrai Visage du Marquis de Sade <BR>(The True Face of the Marquis de Sade)</P><BR>
<IMG SRC="/reynolds/images/E31718.jpg" WIDTH="325" HEIGHT="390" BORDER="1" ALT=""><BR>
<BR>

<P>The Marquis de Sade was admired by the Surrealists for his obscene writings, scandalous theories on the nature of sex, and his commitment to all things pleasurable. In all, Sade spent more than twenty-seven years of his life in various prisons because of his unusual sexual proclivities.</P>
<P>Mary Reynolds coyly alluded to Sade's salient characteristic  by placing the binding in bondage. The black calfskin-covered boards suggest the darkness and perversion of Sade's erotic ideals. The red morocco diagonal bands not only tie up the book, but also suggest welts that result from some of Sade's favored activities.</P><BR>
