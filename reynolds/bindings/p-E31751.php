<P>Arturo Schwarz asserted that this binding is another Mary Reynolds/Marcel Duchamp collaboration.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/bindingsfoot3.php', 'width=250,height=200', 'help')">3</A>] The skin that Reynolds used for the covers is an extremely porous, heavy leather, possibly from the body of an ostrich. The spine is fluted with lighter leather inlays. The binding itself appears to be an ambitious exercise in perspective and spatial relationships, to the detriment of craft. The title is onlaid on the front board on a diminishing plane so that the letters appear to be receding. The back cover with the author's name onlaid is treated in the same manner. When the covers are open and the binding is observed from the spine, two separate planes are apparent. The first plane is the binding itself; the second is the one on which the receding letters rest.</P><BR>
