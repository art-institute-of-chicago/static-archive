<P CLASS=HeadC>Saint Glinglin</P><BR>
<IMG SRC="/reynolds/images/E31735.jpg" WIDTH="295" HEIGHT="350" BORDER="1" ALT=""><BR>
<BR>

<P>This is one of the most playful, yet difficult to understand, of Reynolds's bindings. It may also be the last binding that she created. Saint Glinglin appears to be fictitious, but from the green of the binding and the serpent slithering up the spine with his fangs reaching towards "Glinglin" onlaid in leather letters on the front board, one suspects that, fictitious or not, he may be Irish. This teacup handle may also suggest the reader's ability to pick the book up and pour out its contents.</P>
<P>Queneau was an experimental novelist who drew his inspiration from childhood remembrances. His stories were told in a childlike manner with ironic exaggerations, slapstick wit, and the ubiquitous pun. Queneau was one of Mary Reynolds's close friends. In addition to enjoying his friendship, she must also have enjoyed his fiction: in all, twelve books written by Queneau were bound by Reynolds.</P><BR>
