<BR>

<P>Mary Reynolds bound this sequel to the adventures of Ubu, the puppet king, nearly ten years after binding <i>Ubu Roi</i> in collaboration with Marcel Duchamp. This binding, very reminiscent of that effort, is another plastic pun with the covers of the binding constructed of the letters of the title, "Ubu Cocu." The front and back boards each have a large "U" onlaid in morocco; the spine shows an elongated "b". When the covers are spread open, the cover spells "UbU." The letters "co" appear within the back "U"; the letters "cu" appear through the front "U." These additional onlaid letters in lemon-yellow and gray complete the pun, spelling "UbU CoCu."</P><BR>
