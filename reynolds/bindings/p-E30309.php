<P CLASS=HeadC>Les Mains libres <BR>(The Free Hands)</P><BR>
<IMG SRC="/reynolds/images/E30309.jpg" WIDTH="293" HEIGHT="390" BORDER="1" ALT=""><BR>
<BR>
<P>This binding is one that is most closely associated with the Mary Reynolds Collection: it was chosen to be the cover image for <I>Surrealism and Its Affinities: The Mary Reynolds Collection</I> (Chicago, 1956, 2nd ed. 1973). It is another of Reynolds's and Duchamp's plastic puns. The kid gloves onlaid on both front and back boards are slit open, literally "freeing" the hands. The sumptuousness of the materials, including the silk endpapers, lends an air of elegance to this unexpected and amusing treatment of Man Ray's and Paul &Eacute;luard's stunning collaboration. </P><BR>
