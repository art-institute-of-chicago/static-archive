<P CLASS=HeadC>Un Rude Hiver <BR>(A Harsh Winter)</P><BR>
<IMG SRC="/reynolds/images/E31733.jpg" WIDTH="312" HEIGHT="390" BORDER="1" ALT=""><BR>
<BR>

<P>Mary Reynolds chose to bind this minor novel by Raymond Queneau in traditional morocco leather, and then departed into the realm of fantasy when she applied to the spine a thermometer that appears to be broken at 0 degrees Celsius. The front doublure has an inlaid blue-leather strip indicating cold, while the back doublure has a red-leather strip denoting heat. With these few devices, Reynolds has informed us of the nature of <I>Un Rude hiver.</I></P><BR>
