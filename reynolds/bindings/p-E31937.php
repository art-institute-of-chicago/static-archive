<P CLASS=HeadC>Rrose S&eacute;lavy</P><BR>
<IMG SRC="/reynolds/images/E31937.jpg" WIDTH="482" HEIGHT="350" BORDER="1" ALT=""><BR>
<BR>

<P>In the fall of 1940, Duchamp had a letterpress block made for his almost-completed project <I>Bo&icirc;te-en-valise (Box in a Valise).</I> Approximately three hundred gray cards with "De ou par Marcel Duchamp ou Rrose S&eacute;lavy" were printed for the lid of the inner part of the box. Reynolds similarly used a trial blind stamp with multiple strikes on the same gray cardboard. On both the front and back covers of this binding, she continued to refine her Surrealist vocabulary by bringing unpredictable materials together: rich leathers coupled with common twine and a discarded cardboard proof.</P>
<P>Duchamp created his feminine alter ego, Rrose S&eacute;lavy (a homonym in French for "Eros, c'est la vie," or "Eros, that is life"), in 1920. She was crude, vulgar, and completely irresponsible.</P><BR>
