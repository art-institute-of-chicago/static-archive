<P CLASS=HeadC>La Machine a &eacute;crire <BR>
(The Typewriter)</P> <BR>
<IMG SRC="/reynolds/images/E31717.jpg" WIDTH="350" HEIGHT="385" BORDER="1" ALT=""><BR>
<BR>
<P>The pivotal component of this binding is its pure simplicity, both conceptually and materially. Reynolds used a traditional binding material, vellum, for the spine. It is stamped in gold and, as always, carries Cocteau's star at the bottom. The front and back boards are covered in typewriter paper with upper case M's typed alternately in red and black, symbolizing the <I>machine.</I> Could the "M"s also be a reference to Marcel? Simple, direct, yet the concept is brilliant.</P>

<P>Reynolds considered Cocteau a close friend, even when Breton had banished him from the Surrealist circles. In all, she bound twenty-four books by Cocteau for herself. While some are design bindings, many are simple quarter vellum bindings over decorative boards. All are imprinted in gold with the Cocteau star.</P><BR>
