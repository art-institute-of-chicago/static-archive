<P CLASS=HeadC>Le Rouge et le blanc (Lucien Leuwen) Roman posthume.</P><BR>
<IMG SRC="/reynolds/images/E31738.jpg" WIDTH="350" HEIGHT="378" BORDER="1" ALT=""><BR>
<BR>

<P>Best known for his novel <I>Le Rouge et le noir,</I> at his death Stendhal left an unfinished novel, <I>Le Rouge et le blanc,</I> also known as <I>Lucien Leuwen,</I> which was published in two volumes. Here Mary Reynolds has cleverly covered the spine of the first volume in "rouge" morocco goatskin and the second volume in "blanc" vellum. The author's name runs across both volumes, split between the two&#151;a reference to Stendhal's notorious lack of identity. He spent his life as a wanderer, using 170 pen names during his career, and living his life through his characters and their exploits. By placing "Sten" at the foot of volume one and "dhal" at the foot of volume two, Reynolds acknowledged the lack of defined character and cohesive personality. She presented the author in parts, as Stendhal, in fact, saw himself.</P><BR>
