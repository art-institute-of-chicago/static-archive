<P>William Seabrook's fame rests on his bizarre tales of the supernatural. On the binding for <I>Secrets de la jungle,</I> a collection of exotic stories of primitive cannibals, Mary Reynolds cleverly combined images of flora and fauna from the jungle by her use of botanical imagery made with reptilian leather. The use of a boa constrictor skin suggests the dark mystery of the jungle described in these stories, and hints at the savage cannibalism with which the tales are preoccupied.</P><BR>
