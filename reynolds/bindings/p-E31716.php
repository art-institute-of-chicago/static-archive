<P CLASS=HeadC>Maison de sant&eacute; (Mental Hospital)</P><BR>
<IMG SRC="/reynolds/images/E31716.jpg" WIDTH="450" HEIGHT="305" BORDER="1" ALT=""><BR>
<BR>


<P>The dominant element of this binding is the cut-out star on the front and back covers, which Reynolds adapted from the star that often appeared under Jean Cocteau's signature. In 1916, Cocteau visited the French poet Guillaume Apollinaire (1880&#150;1918) in the hospital where he was recovering from a head wound received in battle. Cocteau, as did many other of the Surrealists, worshipped Apollinaire, considering him one of the seminal influences of the movement. The shape of Apollinaire's scar was the inspiration for Cocteau's signature star.</P>
<P>In 1925, Cocteau was committed to the Urban Baths in Paris for detoxification treatment for his opium addiction. His stay there was the impulse for this book. Reynolds affirmed this association by making the "Cocteau star" the most significant design feature on both the front and back covers. The material that she chose for the binding is a seductive alabaster vellum that suggests the white powder of the narcotic itself. The identification of the star with Cocteau, and Cocteau with opium, cannot be overlooked or ignored: it is key to the book and key to the binding.</P><BR>
