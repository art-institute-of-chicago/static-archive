<P CLASS=HeadC>Les Yeux fertiles <BR>
(The Fertile Eyes)</P> 
<BR>
<IMG SRC="/reynolds/images/E31741.jpg" WIDTH="350" HEIGHT="349" BORDER="1" ALT=""><BR>
<BR>

 
<P>Paul &Eacute;luard, a leading poet of French Surrealism, with his wife Gala, who later married Salvador Dal&iacute;, were close friends of Mary Reynolds and Marcel Duchamp. &Eacute;luard and Reynolds both stayed in Paris during the Occupation and were active together in the French R&eacute;sistance.</P>
<P><I>Les Yeux fertiles</I> is a stunning celebration of love and some of &Eacute;luard's finest poetry. Reynolds's choice of the beautiful yet mysterious skin of the ostrich for the binding suggests the tender yet elusive qualities of love. This binding is the only one in the collection that did not come from Mary Reynolds's own library. It came from the estate of the French poet Georges Hugnet. There is a note in Hugnet's hand indicating that the binding was executed by Mary Reynolds in 1940. Consequently, this is one of the few bindings that can be dated with absolute certainty.</P><BR>
