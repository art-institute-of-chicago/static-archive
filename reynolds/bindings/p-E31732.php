<P CLASS=HeadC>Odile</P> 
<BR>
<IMG SRC="/reynolds/images/E31732.jpg" WIDTH="350" HEIGHT="336" BORDER="1" ALT=""><BR>
<BR>

<P>In addition to the use of elaborate and unpredictable materials, Reynolds employed such devices as unconventional placement of titles and author names to achieve her artistic goals. This binding of black calf with an ivory vellum panel, on which is hand-lettered the title on the front board and the name Queneau on the back, is extraordinarily elegant as well as visually surprising. It demonstrates clearly Reynolds's sound sense of design and proportion as well as her ability to create unpredictable fantasy in a completely unaffected manner.</P>
<P>During his Surrealist period, the French writer Raymond Queneau exhibited a flair for verbal juggling, black humor, puns, and ironic hyperbole. His novels, such as <i>Odile,</I> present familiar settings from which emerges a vision of an absurd world. Queneau later became director of the scholarly <I>Encyclop&eacute;die de la Pl&eacute;iade,</I> an edition of classical authors.</P><BR>
