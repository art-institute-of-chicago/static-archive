<P CLASS=BodySmall> 22. From a psychobiographical point of view, it is important to note that Bellmer's development of the doll coincided with the diagnosis of his wife's tuberculosis in the early 1930s (see note 1 above). In an interview with Peter Webb (<I>The Erotic Arts</I> [Boston, 1975]), Bellmer brought his invention of the dolls into association with his dejection in the 1930s: "Obviously, there was a convulsive flavor to them because they reflected my anxiety and unhappiness" (p. 369). One must look to other factors as well-including of course the menacing political climate in Germany&#151;for the artist's stated unhappiness, but his agonized marital relations should be considered in light of his insistence that the fetishistic dolls "became an erotic liberation for [him]" (ibid.). </P>


