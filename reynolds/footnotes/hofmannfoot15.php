<P CLASS=BodySmall>15. 291, named after Alfred Steiglitz's progressive New York gallery that made its mark as one of the U.S. centers for the avant-garde, was the city's first journal devoted to the work of modern artists. Published from March 1915 to February 1916, the nine issues of 291 were known for presenting the most experimental works in art and design. Some artists featured in 291 include Francis Picabia, Georges Braque, Pablo Picasso and John Marin.</P>
