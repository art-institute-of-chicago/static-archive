<P CLASS=BodySmall> 25. Bellmer in fact derives the organization of his Anatomie de l'image from Schilder's <I>Image and Appearance of the Human Body</I>: Bellmer's three chapters, "Les Images du moi," "L'Anatomie de l'amour," and "Le Monde ext&eacute;rieur," correspond sequentially and thematically to Schilder's three headings, "The Physiological Basis of the Body-Image," "The Libidinous Structure of the Body-Image," and "The Sociology of the Body-Image," respectively. In terms of sources, one wonders too whether Bellmer's "physical unconscious" was in any way a response to Walter Benjamin's notion of an "optical unconscious," introduced in the latter's "Small History of Photography" in 1931. "It is through photography," wrote Benjamin, "that we first discover the existence of this optical unconscious [of the different stages of a bodily movement, as documented, for example, by Eadweard Muybridge], just as we discover the instinctual unconscious through psychoanalysis" (quoted in Rosalind Krauss, <I>The Optical Unconscious</I> [Cambridge, Mass., 1993], p. 178). </P>

