<P CLASS=BodySmall> 7. "Bellmer manipulates the doll," in Hal Foster's words, "as if to ascertain the signs of difference and the mechanics of birth" (see Foster, <I>Compulsive Beauty</I> [Cambridge, Mass., 1993], p. 106). Foster, along with Ren&eacute; R. Held, <I>L'Oeil du psychanalyste: Surr&eacute;alisme et surr&eacute;alit&eacute;</I> (Paris, 1973), p. 311, compares Bellmer with Freud's famous patient Little Hans, whose curiosity about his mother's pregnancy is the subject of Freud's "Analysis of a Phobia in a Five-Year-Old Boy" (1909), in <I>The Standard Edition of the Complete Psychological Works of Sigmund Freud,</I> vol. 10, trans. James Strachey (London, 1955), p. 1&#150;147. Unaware that the elder Bellmer's first name was also Hans, Foster and Held did not fully realize the uncanny aptness of their reference. </P>

