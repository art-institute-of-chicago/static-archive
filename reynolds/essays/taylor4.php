<P CLASS=BodySmall><I>Hans Bellmer in The Art Institute of Chicago:<BR>
The Wandering Libido and the Hysterical Body</I></P><HR SIZE='1' ALIGN='LEFT' NOSHADE WIDTH='250' CLASS=ruleleft>
<P>A passage in Bellmer's <I>Anatomie de l'image</I> mentioned above provides the answer.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot32.php', 'width=250,height=200', 'help')">32</A>] Discussing again the situation of his seated little girl who represses awareness of her sexual parts and transfers it instead to her armpit, the artist asserted the normality of this phenomenon. He wished, however, to introduce extreme, clinical examples of such displacements, and to do this he drew on the dated accounts of the Italian criminologist and physician Cesare Lombroso (1835&#150;1909). Citing the latter's "Transferences of Sensation in Hysteria or Hypnosis,"[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot33.php', 'width=250,height=200', 'help')">33</A>] Bellmer offered cases of adolescent girls whose body images and behavior were severely disturbed by the onset of puberty: one girl's hysterical symptoms included convulsions, hyperaesthesia, and sleepwalking; she became blind but gained the capacity to see through her nose and left ear. "These phenomena," Bellmer continued, "are not isolated":</P>
<P CLASS=Extract>Another girl of fourteen, who also had just begun to menstruate, exhibited convulsive coughs, headache, fainting, spasms, facial convulsions accompanied by singing, sleep lasting sometimes three days, and attacks of somnambulism during which she saw distinctly with her hand and read in the dark.</P>
<P CLASS=Extract>As in the case of the little seated girl, there is an initial conflict between desire and its interdiction, but this time as violent as the crisis of puberty that gave rise to it. Insoluble, this conflict can only lead to the repression of sex, to its projection onto the eye, ear, nose: a projection or displacement that explains to us...the hyperbolic valorization of the sense organs, the dramatization of their functions.</P>
<P><I>Child and Seeing Hands</I> seems quite directly related to these case studies of female hysteria. Like Breton and Louis Aragon, who had celebrated "The Fiftieth Anniversary of Hysteria" in the pages of <I>La R&eacute;volution surr&eacute;aliste</I> in 1928, Bellmer was fascinated by concepts of psychic disorder.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot34.php', 'width=250,height=200', 'help')">34</A>] From the point of view of Surrealism, hysterical symptoms constituted the body's form of automatic writing, an involuntary protest against societal restrictions placed on sexuality. Entrenched for centuries in gender stereotypes&#151;as rational males attempted to fathom the "mysteries" of an unruly female sex&#151;hysteria also fueled Bellmer's long-standing interest in another puzzle: the relation of mind and body.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot35.php', 'width=250,height=200', 'help')">35</A>]</P>
<P>He drew, as we have seen, from Schilder's studies of brain and body image, but also followed Freud on the sexual etiology of the neuroses and incorporated the vocabulary of psychoanalysis in his work. In <I>Child and Seeing Hands,</I> the artist literalized the notion of psychic "defense" with a wall of bricks, and even speculated in <I>Anatomie de l'image</I> about the kind of trauma that might have caused Lombroso's girl to develop the specific symptoms referenced in his drawing:</P>
<P CLASS=Extract>To understand the motif of the second, manifest transference [Bellmer wrote], that of the eye onto the hand for example, one would have to believe that the eye, doubled by the condemned image of the sex, could not entirely conceal the compromising portion of its supplementary content. Let us suppose, without risk of serious error, that some acts of an intimate nature had been seen, heard, perceived&#151;in such a way that, under the influence of shock, repugnance, and a sense of guilt, the transfer or at first simply the loss of vision signifies: "I do not want to see anything, I do not want to see any more.</P>
<P>For Lichtenstein, Bellmer's "account of the materialization of hysterical conversion symptoms" also relates "to the distortions to which he subjected the body of the doll."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot36.php', 'width=250,height=200', 'help')">36</A>] Indeed, many photographs of the second doll, especially those depicting its four-legged incarnation convulsed on a bare floor or bed or arched over in a kind of back bend,[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot37.php', 'width=250,height=200', 'help')">37</A>] recall the thrashing bodies of hysterics documented by the famous nineteenth-century neurologist Jean-Martin Charcot at the hospital of the Salp&ecirc;tri&egrave;re in Paris. Charcot's records, graphic and photographic, helped him to observe and systematize the different stages of hysterical attacks; among the characteristic postures he identified was the <I>arc en cercle,</I> or rainbow pose, with the back arching and both head and feet firmly planted on the ground or (more often) the bed.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot38.php', 'width=250,height=200', 'help')">38</A>] In a turn-of-the-century romance about the Salp&ecirc;tri&egrave;re, one of Charcot's students, Jules Claretie, described a patient enduring one of these seizures&#151;adopting an analogy that uncannily predicts Bellmer's doll: "The human being seemed reduced to the state of a machine, to one of those 'maquettes' of wood that sculptors use, bending the joints of these mannequins all around as they please&#151;macabre caricatures of man."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot39.php', 'width=250,height=200', 'help')">39</A>]</P><a name="E36175"></a>
<BR>

<IMG SRC="/reynolds/images/C36175.jpg" WIDTH="425" HEIGHT="303" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. <I>Untitled</I> (Double-Sided Portrait of Unica Z&uuml;rn) (verso), 1954. Pen and white ink on black wove paper; 50 x 70 cm. The Art Institute of Chicago, Gift of George A. Poole (1967.245).</P>

<P>Sadly and ironically, such attacks were witnessed as well, decades later, by someone whom Bellmer knew intimately, the German Surrealist writer Unica Z&uuml;rn. In 1954, a year after Bellmer met her on a visit to Berlin, Z&uuml;rn moved to Paris with him, and the artist captured her in a typically somber, pensive mood in a portrait now in the Art Institute's collection. On the verso of the sheet, a large, double-sided white-ink drawing on black paper, Z&uuml;rn is shown standing, head lowered, eyes downcast, hands folded demurely in front of her, and dressed in a modest suit. A network of lines and bricks animates the background, while to the right, closer to the picture plane but also implicated in the abstract web, Z&uuml;rn's face emerges in three-quarter view. Inexpressive and wide-eyed, she wears her hair tied back with a bow. Although photographs from the 1950s indicate Bellmer's relative faithfulness to Z&uuml;rn's appearance in this likeness, his obsessive fantasy subsumes her image on the drawing's recto, the side he chose to sign and date.</P>
<a name="E36174"></a>
<BR>
<IMG SRC="/reynolds/images/C36174.jpg" WIDTH="425" HEIGHT="286" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. <I>Untitled</I> (Double-Sided Portrait of Unica Z&uuml;rn) (recto), 1954. Pen and white ink on black wove paper; 50 x 70 cm. The Art Institute of Chicago, Gift of George A. Poole (1967.245).</P>
<P>Here Bellmer transformed her into a <I>femme-enfant,</I> awkwardly displayed in a fanciful, transparent, pleated dress reminiscent of the one worn by the girl in <I>Child and Seeing Hands</I>; he also depicted her on all fours in striped stockings and Mary Janes. In the latter pose, the figure plays with a cat's-eye marble&#151;a motif Bellmer introduced twenty years earlier in "Memories of the Doll Theme." Her pink hair-bow is real, the drawing's single collage element and a staple feature in Bellmer's iconography of little girls.</P>
<P>Z&uuml;rn left Bellmer temporarily in 1960 and returned to Germany. There she was admitted for the first time to a mental institution, diagnosed as schizophrenic. In the decade remaining to her, she was hospitalized intermittently in France, where she continued to live with Bellmer and to produce autobiographical texts, including the important <I>Der Mann im Jasmin: Eindr&uuml;cke aus einer Geisteskrankheit (The Man in the Jasmine: Impressions from a Mental Illness) </I>and <I>Dunkler Fr&uuml;hling (Dark Spring).</I> In her writings, Z&uuml;rn presented a poignant picture of illness from within the institution, from the point of view of the patient, which stands in contrast to the constructions of female madness in the diverse work of Charcot, Claretie, Breton, Aragon, and Bellmer.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot40.php', 'width=250,height=200', 'help')">40</A>] At moments, however, she thought of Bellmer's monstrous inventions in terms of what she saw around her, detecting in the body of a fellow patient "a connection with his celebrated 'Cephalopod,' the woman with head and legs and without arms."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot41.php', 'width=250,height=200', 'help')">41</A>] Speaking of herself in the third person, Z&uuml;rn related:</P>
<P CLASS=Extract>She saw this monster at [the hospital of] St. Anne: a mentally ill woman, in an erotic seizure, surrendering herself to her imaginary partner....As if Bellmer were a prophet, the sick woman was horrible to see. All was in upheaval: her legs and back in the shape of an arc, and the terrible tongue; scenes of madness, of torture, and ecstasy: [these] are depicted by him with the sensitivity of a musician, the precision of an engineer, the rawness of a surgeon.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot42.php', 'width=250,height=200', 'help')">42</A>]</P>
<P>During the course of their difficult relationship, Bellmer and Z&uuml;rn collaborated in a number of ways, he providing postscripts and frontispieces for her publications, she posing for drawings and at least one painting.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot43.php', 'width=250,height=200', 'help')">43</A>] In 1958 he made a group of shocking black-and-white photographs of her naked torso bound tightly with string, transforming her body into a series of folds and bulging mounds of flesh. One of these was reproduced on the cover of <I>Le Surr&eacute;alisme, m&ecirc;me</I> that year: showing Z&uuml;rn reclining on a bed, seen from behind without head, arms, or legs&#151;leaving only a pale lump of trussed meat&#151;the image bears the necrophilic caption "keep in a cool place."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot44.php', 'width=250,height=200', 'help')">44</A>] Like all of Bellmer's work, these photographs manipulate and distort the female body for the purposes of a complexly motivated male imaginary. Z&uuml;rn was not the only model ever to place herself in the service of this compulsive project, but her tragic psychological vulnerability, which culminated in suicide in 1970, renders it especially disturbing. Reflecting on Bellmer's creative endeavors, she pointed to his extraordinary technical facility, "infinite gentleness," and circumspection, and yet admitted that the person "who is sketched by him, or photographed...by his pencil [<i>sic</i>] participates with Bellmer in the abomination of herself. Impossible for me," she concluded chillingly, "to render him greater praise."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot45.php', 'width=250,height=200', 'help')">45</A>]</P><BR>


