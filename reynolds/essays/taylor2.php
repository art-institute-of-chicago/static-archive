<P CLASS=BodySmall><I>Hans Bellmer in The Art Institute of Chicago:<BR>
The Wandering Libido and the Hysterical Body</I></P><HR SIZE='1' ALIGN='LEFT' NOSHADE WIDTH='250' CLASS=ruleleft>
<P>In one image, for example, the doll is a tragic amputee, armless and tied on a shadowy stairway with frayed twine.</P><a name="E31675"></a>
<BR>
<IMG SRC="/reynolds/images/E31675.jpg" WIDTH="350" HEIGHT="354" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. Plate 12 of <I>Les Jeux de la poup&eacute;e (The Games of the Doll).</I> Paris, Les Editions premi&egrave;res, 1949. Hand-colored black-and-white photograph.</P>
<P>With a second (reversed) pelvis substituting for its chest, the doll is given buttocks for breasts, and these seem incongruously large, considering the undeveloped pudenda and the juvenile hair-bow. The doll's left leg is bound at the knee, while the right thigh ends abruptly in midair, exposing a hollow core. All is passive, inert: one hand lies limply against the banister, and a blank, unseeing eye suggests a loss of consciousness. Who, one wonders, is responsible for the naked and abject condition of the doll?[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot9.php', 'width=250,height=200', 'help')">9</A>] While the brutality implicit in such images is often elided in the literature, Bellmer's biographer, Peter Webb, detected "intimations of rape" in another, singularly bizarre photograph of the doll, partly lying, partly seated, on a rumpled bed, and half dressed in a man's trousers with the belt and fly provocatively open.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot10.php', 'width=250,height=200', 'help')">10</A>]</P><a name="E31678"></a>
<BR>
<IMG SRC="/reynolds/images/E31678.jpg" WIDTH="350" HEIGHT="354" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. Plate 9 of <I>Les Jeux de la poup&eacute;e (The Games of the Doll).</I> Paris, Les Editions premi&egrave;res, 1949. Hand-colored black-and-white photograph.</P>
<P>Here the doll is composed of two sets of hips and legs&#151;with feet shod in shiny Mary Janes&#151;around the central ball joint of the exposed abdomen. Some kind of sexual struggle seems underway, yet the remains of a meal on a table next to the bed give a sense of satiation, and perhaps the pants have been loosened merely to ease the pressure on a bulging stomach.</P>
<P>Such ambiguous readings are not easily resolved, and in fact the power of these photographs often resides in the coexistence and confusion of the perverse and the banal. The peculiar combination of male and female in the postprandial image has prompted speculations about hermaphrodism, while Bellmer scholar Therese Lichtenstein noticed "a highly complex, contradictory dynamic in which issues of individuation, separation, and symbiotic union are played out in terms of a kind of identity crisis."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot11.php', 'width=250,height=200', 'help')">11</A>] The scene has the quality of nightmare: with its eerie shadows, disheveled bed linens, vague domesticity, and aura of voyeurism, it could be said to stage a troubled fantasy of the primal scene. Indeed, the uncertainty of the child about the possibly violent "struggle" of his parents in bed is echoed in the confusion aroused by this photograph. The elevated point of view has a destabilizing effect, tipping the floor toward the picture plane so that food, figure, and mattress seem about to slide off to the lower left. The feeling of vertigo is exacerbated by the insistent downward-left direction of the stripes on the bed covers and tablecloth, the knives on the empty plate, and the fringed edges of the carpets.</P>
<P>In late 1935 or early 1936, Bellmer sent a number of the photographs of the second doll to Paris, where they were received by Breton, Paul &Eacute;luard, Henri Parisot, and Valen&ccedil;ay, and soon appeared in <I>Minotaure</I> and in an issue of <I>Cahiers d'art</I> devoted to the Surrealist object.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot12.php', 'width=250,height=200', 'help')">12</A>] When the artist moved from Berlin to Paris, following the death of his wife in February 1938, &Eacute;luard selected fourteen of the photographs, including the two discussed above, and "illustrated" each of them with a short poem.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot13.php', 'width=250,height=200', 'help')">13</A>] These images and their corresponding verses were to be published by Christian Zervos in 1939, but when war with Germany broke out, the project collapsed;[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot14.php', 'width=250,height=200', 'help')">14</A>] Bellmer managed only to produce, on his own, a tiny edition of the book, <I>Poup&eacute;e II.</I> Ten years passed before the full collaborative work was published, by Editions Premi&egrave;res in Paris, as <I>Les Jeux de la poup&eacute;e (The Games of the Doll).</I> The Art Institute owns number 118 in this small edition of 136.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot15.php', 'width=250,height=200', 'help')">15</A>]</P><a name="E31666"></a>
<BR>
<IMG SRC="/reynolds/images/E31666.jpg" WIDTH="303" HEIGHT="390" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer and Paul &Eacute;luard. <I>Les Jeux de la poup&eacute;e (The Games of the Doll).</I> Paris, Les Editions premi&egrave;res, 1949. Fifteen hand-colored black-and-white photographs by Bellmer tipped in, fourteen verses by &Eacute;luard. Black construction-paper cover over board with pink construction-paper belly band, ed. 118/136; 25 x 19.5 x 1.5 cm. </P>
<P>On the black construction-paper cover of the book, the artist affixed an image of the second doll, reduced to its spherical belly radiating two pelvises; Bellmer cut the image out of a black-and-white photograph and hand-colored it in yellow and purple. A pink band wraps around the cover, indicating the book's authors and title.</P>
<P>Just as he had included "Memories of the Doll Theme" with photographs of his first doll in both the German and French editions of <I>The Doll,</I> Bellmer provided an introductory essay, "Notes on the Subject of the Ball Joint," for <I>Les Jeux de la poup&eacute;e.</I> He had written the latter essay&#151;bizarre and convoluted, in a pseudoscientific tone&#151;in German in 1938, and revised it slightly while working with Georges Hugnet to translate it into French in 1946.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot16.php', 'width=250,height=200', 'help')">16</A>] Hugnet, a Surrealist artist and poet, also had a bookbinding studio in Paris from 1934 to 1940; during that time, he and Bellmer had collaborated on yet another <I>livre d'artiste</I> in the Mary Reynolds Collection, the exquisite little <I>Oeillades cisel&eacute;es en branche (Glances Cut on the Branch)</I> (Paris, 1939), with its distinctive pink cover wrapped in a white paper doily.</P><a name="E31662"></a>
<BR>
<IMG SRC="/reynolds/images/E31662.jpg" WIDTH="350" HEIGHT="335" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer and Georges Hugnet. <I>Oeillades cisel&eacute;es en branche (Glances Cut on the Branch).</I> Paris: Editions Jeanne Bucher, 1939. Text by Hugnet with twenty-five hand-colored illustrations by Bellmer. Printed in heliogravure and bound with pink paper and a white paper doily, ed. 93/230; 13.5 x 10.8 x .6 cm.</P>
<P>Adding to the object's synaesthetic allure, the first thirty copies of the edition of two hundred and thirty were impregnated with perfume.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot17.php', 'width=250,height=200', 'help')">17</A>] To Hugnet's handwritten, poetic text adulating pubescent females, Bellmer juxtaposed twenty-five drawings of little girls at play, reproduced in heliogravure in pink, brown, and olive green.</P>
<P>The very daintiness of this presentation, together with Bellmer's delicate draftsmanship, belies the indecent aspects of the book's content. In certain illustrations, girls are shown innocently engaged, with hoops and diabolos, riding on scooters, in pleated skirts or jumpers; in some instances, however, they are naked, with articulated, doll-like bodies, or are clad in seductive undergarments and high-heeled boots. Projecting his own prurience onto his subjects, Bellmer included one drawing of a girl, legs lifted, examining her genitals in an ornate mirror. There is also something suggestive, moreover, about the book's peculiar title. The original French phrase, <I>oeillades cisel&eacute;es en branche,</I> was borrowed from a sign in a market offering bunches of grapes still attached to branches from the vine.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot18.php', 'width=250,height=200', 'help')">18</A>] Bellmer and Hugnet responded to the double meaning of "<I>oeillades</I>"&#151;"bunches" and "glances"&#151;especially since an <I>oeillade</I> can be an ogling glance, a leer. Embedded in this punning, found phrase are implications of the desiring male gaze and perhaps, too, wishes to "harvest" the budding girlhood sexuality that is the theme of the book.</P>
<P>One of the stranger and more detailed illustrations to <I>Oeillades cisel&eacute;es en branche</I> is key, when brought into association with a related passage in "Notes on the Subject of the Ball Joint" mentioned above, to Bellmer's idiosyncratic conceptions of body image and libidinal experience. On page 28 of the book, an articulated female doll, naked except for a filmy blouse, thigh-high stocking, and lace-up boot, is shown seated at a round table, holding an apple aloft in one hand.</P>
