<P CLASS=HeadA>Hans Bellmer in The Art Institute of Chicago:<BR>
The Wandering Libido and the Hysterical Body</P>
<P CLASS=author>SUE TAYLOR</P>
<P CLASS=authorlocation>Department of Art History, The University of Chicago</P><a href="E31661"></a>
<P>In December 1934, there appeared in the Surrealist journal<I> Minotaure</I> a two-page spread introducing French readers to the erotic imagination of the German artist Hans Bellmer. Eighteen photographs Bellmer had taken of a life-size, female mannequin are grouped symmetrically around the title "Doll: Variations on the Montage of an Articulated Minor."</P>

<P>The images show Bellmer's assemblage, made of wood, flax fiber, plaster, and glue, under construction in his studio or arrayed on a bare mattress or lacy cloth. Seductive props sometimes accompany the doll&#151;a black veil, eyelet undergarments, an artificial rose. Naked or, in one case, wearing only a cotton undershirt, the armless doll is variously presented as a skeletal automaton, a coy adolescent, or an abject pile of discombobulated parts. In one unusual image, the artist himself poses next to his standing sculpture, his human presence rendered ghostly through double exposure. Here Bellmer's own body seems to dematerialize as his mechanical girl, wigged, with glass eyes, wool beret, sagging hose, and a single shoe, takes on a disturbing reality.</P>
<BR>
<IMG SRC="/reynolds/images/E31661.jpg" WIDTH="425" HEIGHT="273" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. "Poup&eacute;e, variations sur le montage d'une mineure articul&eacute;e," <I>Minotaure</I> 6 (Winter, 1934&#150;35), pp. 30&#150;31.</P>
<P>The Surrealist fascination with automata, especially the uncanny dread produced by their dubious animate/inanimate status, prepared the way for the enthusiastic reception in France of Bellmer's doll. His stated preoccupation with little girls as subjects for his art, moreover, coincided with the Surrealist idealization of the <I>femme-enfant,</I> a muse whose association with dual realms of alterity, femininity and childhood, inspired male artists in their self-styled revolt against the forces of the rational. But these dovetailing concerns&#151;of the artist-intellectuals grouped around Andr&eacute; Breton in Paris, and of the isolated Bellmer, working in virtual seclusion in a suburb of Berlin&#151;were arrived at separately. Bellmer's doll, the first sculptural construction of an erstwhile graphic designer, developed out of a series of three now legendary events in his personal life: the reappearance in his family of a beautiful teenage cousin, Ursula Naguschewski, who moved to Berlin from Kassel in 1932; his attendance at a performance of Jacques Offenbach's <I>Tales of Hoffmann,</I> in which the protagonist falls tragically in love with the lifelike automaton Olympia; and a shipment from his mother of a box of old toys which had belonged to him as a boy. Overwhelmed with nostalgia and impossible longing, Bellmer acquired from these incidents a need, in his words, "to construct an artificial girl with anatomical possibilities...capable of re-creating the heights of passion even to inventing new desires."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot1.php', 'width=250,height=200', 'help')">1</A>]</P>
<P>Bellmer celebrated his invention of the doll in a delirious essay, "Memories of the Doll Theme" (1934): "It was worth all my obsessive efforts," he wrote, "when, amid the smell of glue and wet plaster, the essence of all that is impressive would take shape and become a real object to be possessed."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot2.php', 'width=250,height=200', 'help')">2</A>] In their explicit sexual implications, the images of "young maidens" he put forth in this essay depart dramatically from the ideal of the innocent <I>femme-enfant.</I> Bellmer imagined little girls engaged in perverse games, playing doctor in the attic; he meditated lasciviously on "their bowed and knock-kneed legs" and "the casual quiver of their pink pleats"; and he despaired "that this pink region," like the pleasures of childhood itself enjoyed in the maternal plenitude of a "miraculous garden," was forever beyond him. In closing his essay, Bellmer took revenge on little girls for their unavailability, envisioning the manufacture of the doll in their image, which he probed "with aggressive fingers" and "captured rapaciously by [his] conscious gaze."</P>
<P>The pink pleats, Bellmer's poetic metaphor for female genitals, would become a recurring motif in his drawings and prints, and the female body itself, with the exception of a small number of portraits of friends,[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot3.php', 'width=250,height=200', 'help')">3</A>] remained Bellmer's sole artistic subject&#151;to the point of obsession. A fantastic drawing in the Art Institute's collection, executed in white gouache on black paper in 1936, is an early indication of the fetishistic metamorphoses of the female body Bellmer produced throughout his career.</P><a name="E26500"></a>
<BR>
<IMG SRC="/reynolds/images/E26500.jpg" WIDTH="252" HEIGHT="390" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. <I>Untitled,</I> 1936. Pen and white gouache on black wove paper; 24.9 x 15.9 cm. The Art Institute of Chicago, Mr. and Mrs. Joseph Randall Shapiro Collection (1991.197).</P>
<P>Here pleated skirts swirl around a confusion of anatomical fragments: two or more bodies seem to be suspended from columns or to float in air; a breast, buttocks, legs in striped hose, and high-heeled boots all blend with phallic forms in a grotesque turmoil. At the lower left, a girl's face emerges in profile from a deluge of flowing lines, which double as hair and as a watery cascade emanating from a mountainous landscape in the distant background. This cursory landscape and the curious detail of a rearing horse at the right of Bellmer's drawing suggest his close study of Italian and Northern Renaissance graphics in Berlin's Kaiser Friedrich Berlin Museum;[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot4.php', 'width=250,height=200', 'help')">4</A>] but, even if this image originated in some figure of Saint George rescuing his princess, Bellmer's fascination with sexual themes, as always, renders the subject marginal.</P>
<P>He presented his essay "Memories of the Doll Theme" in <I>Die Puppe (The Doll),</I> a booklet printed in a very small edition at the artist's expense by his friend Thomas Eckstein in Karlsruhe, with ten photographs of the doll tipped in by hand, as in a family album.[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot5.php', 'width=250,height=200', 'help')">5</A>] The photographs Bellmer selected for inclusion in this precious <I>livre d'artiste</I> were among those later published as "Variations on an Articulated Minor," beginning with the gaunt framework of the doll in its initial stages of construction and ending with a still-life arrangement of detached plaster legs surrounded by white undergarments, a rose, and a single high-heeled pump. The tiny size of the edition and of the object itself lends a secretive quality to Bellmer's project, which was possibly a function of necessity in Germany, where the Nazis had assumed power and were increasingly suspicious of modern artists. Not long after his <I>Minotaure</I> debut, Bellmer visited Paris, gaining an introduction to Breton; on his return to Berlin, he began to collaborate through the mail with the Surrealist writer Robert Valen&ccedil;ay on an exacting French translation of "Memories of the Doll Theme." In 1936 a limited French edition of <I>Die Puppe, La Poup&eacute;e,</I> was issued by Guy L&eacute;vis-Mano in Paris; a copy of this book is preserved in the Art Institute's Mary Reynolds Collection.</P><a name="E31664"></a>
<BR>
<IMG SRC="/reynolds/images/E31664.jpg" WIDTH="295" HEIGHT="390" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. <I>La Poup&eacute;e (The Doll).</I> Paris, Editions GLM, 1936. Translated from the German by Robert Valen&ccedil;ay. Printed on pink paper, with ten original photographs tipped in on tan paper, ed. 36/100; 16.7 x 12.8 x .5 cm.</P>
<P>As in the German edition, Bellmer reproduced his essay on delicate pink pages, with a linocut of the doll's torso illustrating a rotating "peep-show" mechanism he planned to incorporate in the belly of the actual doll, operated by pressing the figure's left nipple.</P>
<a name="E31665"></a>
<BR>
<IMG SRC="/reynolds/images/E31665.jpg" WIDTH="284" HEIGHT="360" BORDER="0" ALT=""><BR>
<P CLASS=caption>Hans Bellmer. <I>La Poup&eacute;e (The Doll),</I> detail. Paris, Editions GLM, 1936. Linocut on pink paper; 16.7 x 12.8 cm.</P>
<P>Although this plan was never realized, the voyeuristic drive that propelled it survives in the linocut, where an inquisitive, disembodied eye peers through the doll's navel, and a dark hand pokes the doll's breast. This image of male sexual curiosity and domination is in a sense emblematic of Bellmer's entire oeuvre: the hapless female body, deprived of head and limbs, is scrutinized and manipulated, and its inner workings exposed in a cut-away view. "Lay bare suppressed girlish thoughts," the artist instructed himself, "ideally through the navel, visible as a colorful panorama electrically illuminated deep in the stomach."[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot6.php', 'width=250,height=200', 'help')">6</A>] And just as Bellmer revisited an infantile world in "Memories of the Doll Theme"&#151;one of magic kits, Easter eggs, puppets, and cat's-eye marbles&#151;tinged with sexuality, in this linocut he seems to have posed quintessential childish questions: "How are girls different?" and "Where do I come from?"[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot7.php', 'width=250,height=200', 'help')">7</A>]</P><a name="E31675"></a>
<P>In order better to manipulate the doll, Bellmer developed a second version which he organized around the principle of the ball joint. Inspired by a pair of sixteenth-century articulated wooden dolls in the Kaiser Friedrich Museum,[<A CLASS=blue HREF="javascript:launchHelp('/reynolds/footnotes/taylorfoot8.php', 'width=250,height=200', 'help')">8</A>] he produced a spherical belly for the new doll, around which could be arranged a number of parts in various combinations: four legs, four round stylized breasts, an upper torso, three pelvises, a pair of arms, and the recycled head and hand from the first doll. From 1935 through 1938, this second figure, in its numerous permutations, appeared in over one hundred photographs, many hand-colored to heighten their emotive impact. Unlike the photographs of the first doll, which documented the construction of the object or treated it as still life, Bellmer's photographs of the ball-jointed doll establish sinister, narrative tableaus, with new emphasis on the doll's environment: outside the studio, the doll becomes a dramatic character, often the victim of an unseen tormentor, in domestic interiors, basement, hayloft, or forest.</P>
