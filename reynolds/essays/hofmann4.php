<P CLASS=HeadB>Surrealism in New York</P>
<P>The outbreak of World War II brought many of the Surrealists to New York: Dal&iacute;, Man Ray, Matta, and Yves Tanguy had all arrived by 1940. With several European reviews suspended or increasingly inaccessible due to the war, the Surrealists in New York immediately attempted new publications. In September 1940, the first issue of <I>View</I> magazine, edited by Charles Henri Ford, was published. Thirty-one subsequent issues appeared between 1940 and 1947. <I>View</I> offered coverage of art, literature, music, and cinema&#151;anything that was new and modern. As one of its slogans read: "You can't be modern and not read <I>View.</I>" While the scope of this journal was broad, at times <I>View</I> gave particular attention to the activities of the Surrealists: Duchamp was on the advisory board, Mary Reynolds was listed as the journal's Paris Representative, and issue number 7&#150;8 of <I>View</I> (October&#150;November 1941) was dedicated to Surrealism, featuring the art of Artaud, Victor Brauner, Leonora Carrington, Duchamp, and Masson.</P>
<P>Another New York journal that represented Surrealism was <I>VVV,</I> published by the young American sculptor David Hare. With Breton, Ernst, and Duchamp as editorial advisors, <I>VVV</I> gave exiled Surrealist writers and artists great exposure in the United States. Modeled on <I>Minotaure</I> and more substantial than <I>View,</I> <I>VVV</I>'s three issues feature "Poetry, plastic arts, anthropology, sociology, psychology." The first issue (October 1942) has a cover design by Ernst and includes writing by Breton. Reflecting new connections within the New York art community, this issue also featured contributions by artist Robert Motherwell and critic Harold Rosenberg. The next issue, a double number (March 1943), has front and back covers by Duchamp.</P><a name="E31719"></a>
<BR>

<IMG SRC="/reynolds/images/E31719.jpg" WIDTH="321" HEIGHT="390" BORDER="0" ALT=""><BR>
<P CLASS=caption><I>VVV</I> 2&#150;3, ed. David Hare (New York, March 1943), cover.</P>
<P>The front cover is an anonymous etching representing an allegory of death that Duchamp appropriated. The back cover features the shape of a woman's profile cut out of the cover with a piece of chicken wire inserted in the opening. The final issue of <I>VVV</I> (February 1944) is similarly creative and dynamic. With a bold cover designed by Matta, this issue features many fold-out pages of varying sizes, a combination of different papers, and many color images.</P>
<P>In addition to extending the life of the Surrealist movement, American reviews such as <I>View</I> and <I>VVV</I> provided a forum for communication between the Surrealists and a growing number of emerging American artists. For artists who later would make up the Abstract Expressionist group, the Surrealists were a significant and liberating influence. While Surrealism's potency was in decline by this time, artists of the next generation would continue to explore its tenets.</P>
<P>Although neither Dada nor Surrealism revolutionized society as profoundly as their proponents had hoped, they left an indelible mark on art and writing. These iconoclastic impulses of these movements remain rich sources of artistic inspiration. The remarkable journals they generated have preserved a detailed record of the revolutionary atmosphere in which they were conceived and generated. Through their journals, the Dadaists and Surrealists defined and broadcast their views of the world, and expressed their hopes to transform and liberate art and culture. For admirers of the rich and revolutionary ideas of these movements, these journals offer unique insights into the minds of their creators.</P><BR>


