<P CLASS=HeadA>Mary Reynolds: <BR>From Paris to Chicago to the Web</P>
<P CLASS=author>JACK PERRY BROWN</P><P CLASS=authorlocation>Director, Ryerson and Burnham Libraries<BR>
The Art Institute of Chicago</P>
<P>It is a truism that Chicago collectors have long had a passion for Surrealism. Although this was evident in The Art Institute of Chicago's galleries of twentieth-century painting and sculpture, it was only with the reinstallation in 1993 of the museum's holdings of art from 1900 to 1950 that the breadth and range of Chicago's interest in the movement became clear. It was now possible to enrich the paintings and sculptures with items in other media, media that are often called minor or considered ephemeral but that, in many ways, were central to the enterprise of the Surrealists: found objects, collaged objects, printed books, photographs, posters, manifestos, postcards, and the like.</P>
<P>In the course of considering the resources available to tell this tale, Charles Stuckey, then Curator of Twentieth-Century Painting and Sculpture, and Susan Godlewski, then Associate Director of Ryerson and Burnham Libraries, determined to devote a gallery to these other manifestations of the Surrealist spirit in many media. The response to this display has been very strong&#151;the texts, bookbindings, photographs, and objects shown on a rotating basis have a force and an immediacy that illuminate the world of Surrealism in a direct and enticing way.</P>
<P>The items cared for by the Art Institute's libraries that are now shown in Gallery 245 have been in the museum for years but, like many fragile objects, have been stored and not displayed. They have been available to those who knew of their existence, and have been documented in a catalogue now long out of print (Hugh Edwards, ed., <I>Surrealism and Its Affinities: The Mary Reynolds Collection</I> [Chicago, 1956; 2nd ed., 1973]). Thus, we are grateful not only to be able to show selections in the galleries but also to present some of them in context and in depth in this web-based updating of selected articles from <I>Museum Studies,</I> vol. 22, no. 2.</P>
<P>The library's Surrealist holdings are chiefly in the Mary Reynolds Collection, presented to the Art Institute in 1951 by her brother, Trustee Frank B. Hubachek. Marcel Duchamp, who had shared his life with Reynolds for a quarter of a century, strongly supported the idea of the gift and designed a bookplate for the collection, here adapted as the logo for this website. <A HREF="https://digital-libraries.artic.edu/cdm/ref/collection/mqc/id/1067">Duchamp's preface</A>  to the 1956 edition of <I>Surrealism and Its Affinities</I> gives us as personal a glimpse of Reynolds (and of Duchamp) as we may ever have. I also acknowledge the work of my predecessors in the Ryerson and Burnham Libraries who have enriched and extended the original Reynolds Collection, adding titles and completing periodical runs to bring the <A HREF="https://artic-primo.hosted.exlibrisgroup.com/primo-explore/browse?vid=01ARTIC&browseQuery=709.04063%20R46&browseScope=callnumber.1&innerPnxIndex=-1&numOfUsedTerms=-1&fn=BrowseSearch" TARGET="_blank">collection catalog</A> from 350 titles in 1956 to just under 600 titles today.</P>
<P>Mary Reynolds, as Susan Godlewski demonstrates in her empathetic essay, was, in her quiet way, a central figure in the Surrealist milieu and an artist of wit and distinction in her own chosen medium of book binding, a "minor art" that enjoyed a major vogue in the 1920s and 1930s. Reynolds's fascinating personal and artistic life, previously recorded only in snippets and brief references in the diaries and memoirs of her contemporaries, reveals a woman who was an active participant in the major cultural centers of New York and Paris, but one who also served as a member of the French R&eacute;sistance in the heart of Nazi-occupied Paris long after others had fled. It is a pleasure to give Mary Reynolds her full place in the roster of the Surrealists and to acknowledge the years of study and research that enabled Susan Godlewski to accomplish this.</P>
<P>Surrealism was not really about fine art, but aimed to become an all-encompassing attitude that could affect&#151;if not a mass audience&#151;at least other artists, through literary means such as posters, manifestos, poetry, literature, and magazines. Irene Hofmann, who has organized the personal letters, records, and documents in the <A HREF="https://digital-libraries.artic.edu/cdm/search/collection/findingaids/searchterm/Reynolds,%20Mary,%20Collection,%201892-1972%20%20bulk%201941-1964%20/field/title/mode/all/conn/and/cosuppress/1">Mary Reynolds Archive</A> and worked extensively with the collection itself in mounting exhibitions in Gallery 245, surveys in her essay the complex geographic and intellectual history of the chief journals produced by Dada and Surrealist artists in Germany, France, and the United States from 1920 to the late 1940s. (Reynolds herself served as the Paris representative for <I>View,</I> one of the American Surrealist titles, when she returned to her beloved France after World War II.)</P>
<P>In <I>Minotaure,</I> one of the periodicals Hofmann discusses, appears the work of Hans Bellmer, a German artist whose own monographic&#151;one is tempted to say monomaniacal&#151;publications focusing on the female body in photographs of dolls and artists' lay-figures are strongly represented in the Reynolds Collection and in other Art Institute holdings. Sue Taylor, completing her dissertation in the Department of Art History at the University of Chicago when she wrote her article, places Bellmer and his art firmly in their artistic and psychoanalytic contexts, illuminating our understanding and perhaps reminding us that art is not concerned solely with the beautiful, but also with all aspects of the human psyche, a subject of prime interest to the Surrealists.</P>
<P>I am grateful to the authors for their thoughtful work with this rich collection; to Michael Sittenfeld, editor of <I>Museum Studies,</I> for his interest and support in preparing the original print issue; to Terry Ann R. Neff, for her astute reading and editing of the articles; to Barbara Korbel, Conservation Librarian, and her staff for their excellent craft in preparing the frequently problematic objects for photography and for rotating display in Gallery 245; to Anne Champagne, Head of Technical Services in the Ryerson and Burnham Libraries, who supervised the bibliographic cataloging (and did much herself) that underlies this entire project; and to Daniel Schulman, then Assistant Curator of Twentieth-Century Painting and Sculpture, for his insightful reading of the manuscripts. Paul Baker and Todd Reifenrath of Webitects.com accomplished the transition from print to web elegantly, sympathetically, and on budget. Their design acknowledges the work of Anne Wassmann for <I>Museum Studies.</I></P>
<P>But I am most deeply grateful to Marjorie Hubachek Watkins, Mary Reynolds's niece. Her gift of additional letters, photographs, family papers and memorabilia documenting Mary Reynolds's life, the gift of her collection to the Art Institute by Frank B. Hubachek, and her financial support have enabled us to provide you with this Surrealist sampler. Equally important, her memories of "Aunt Mary" in Chicago, at her family's camp in Minnesota, and in France, and her sustained interest over time have enriched our understanding of the collection and of Mary Reynolds's contributions to the Surrealist movement.</P>
<P>And lastly, we are grateful to the Gladys Krieble Delmas Foundation for its ready support in translating this initial part of the Mary Reynolds Collection to the web. Although selections from the nearly 600 pieces in the Reynolds Collection are on view in the Art Institute galleries on a rotating basis and most of the rest of the collection can be viewed in the Ryerson and Burnham Libraries by appointment, we feel strongly that the web will make a congenial, accessible and ever-expanding home for Mary Reynolds's gentle, Surrealist spirit. Here new titles for the collection will be added to an always-current catalog, additional images will provide ready access to fragile and complex items, and future scholarship will find a home.</P>
