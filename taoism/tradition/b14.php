<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Mirror with Animals of the Four Directions and the Zodiac</CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Mirrors, believed to reflect the true nature of the world, served as symbolic sources of light for the dead on their journey through the underworld. As a result, the backs of mirrors were often decorated with <A HREF="javascript:launchHelp('/taoism/glossary/cosmology.php', 'width=250,height=275')">cosmological</a> designs and symbolic maps of the universe.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Around the central knob of this mirror are the animal symbols of the four cardinal directions: the green dragon of the east (<A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yang</a>); the vermilion bird of the south (yang); the white tiger of the west (<A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin</A>); and the "dark warrior" of the north, an entwined tortoise and snake (yin). These animals represent the division of space into four directions and the division of time into four seasons.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>In a wide band around the edge of the mirror, the 12 animals of the <A HREF="/taoism/diag-zodiac.php" TARGET="_blank">Chinese zodiac</A> are arranged clockwise: rat, ox, tiger, hare, dragon, snake, horse, ram, monkey, cock, dog, and boar. Each represents a section of the sky through which Jupiter passes during its 12-year rotation around the sun. These symbols were traditionally used by the Chinese to mark not only the passage of years but also the parts of the day, which was divided into 12 sections.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Taken as a whole, the decorations on the back of the mirror represent traditional divisions of time and space, thus serving as a map and a calendar. These concepts were among the most important foundations of Taoist cosmology.</P>
