<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>
	
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Taoist <A HREF="javascript:launchHelp('/taoism/glossary/cosmology.php', 'width=250,height=275')">cosmology</a> was shaped by the way in which the Chinese traditionally understood the world. Taoists believe that when the world began, there was only the <A HREF="javascript:launchHelp('/taoism/glossary/tao.php', 'width=250,height=275')">Tao</a>, a featureless, empty void pregnant with the potential of all things. At this point, the Tao generated swirling patterns of cloudlike energy, called <I><A HREF="javascript:launchHelp('/taoism/glossary/qi.php', 'width=250,height=275')">qi</a></I> (pronounced "chee"). This energy eventually developed two complementary aspects: <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin</a>, which is dark, heavy, and feminine, and <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yang</a>, which is light, airy, and masculine. Yin energy sank to form the earth, yang energy rose to form the heavens, and both energies harmonized to form human beings. Consequently, the human body holds within it the energies of both the earth and the heavens, making it a <A HREF="javascript:launchHelp('/taoism/glossary/microcosm.php', 'width=250,height=275')">microcosm</a> of the world. Both yin and yang split further into subdivisions known as the <A HREF="javascript:launchHelp('/taoism/glossary/five-phases.php', 'width=250,height=275')">Five Phases</a>, which can be understood through their associations with the elements, seasons, and directions:</P>
<P CLASS=Extract>greater yang: wood and spring (east)<BR>
lesser yang: fire and summer (south)<BR>
greater yin: metal and autumn (west)<BR>
lesser yin: water and winter (north)<BR>
the central phase: earth and the <A HREF="javascript:launchHelp('/taoism/glossary/solstice.php', 'width=250,height=275')">solstices</a>
</P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The central phase represents a balance of yin and yang.
</P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The primary symbols of yin and yang in ancient China were the <A HREF="javascript:launchHelp('/taoism/glossary/tiger-and-dragon.php', 'width=250,height=275')">white tiger and green dragon</A>, also symbols of autumn and spring, respectively. By the <A HREF="javascript:launchHelp('/taoism/glossary/song-dynasty.php', 'width=250,height=275')">Song dynasty</a>, the <A HREF="javascript:launchHelp('/taoism/glossary/taiji.php', 'width=250,height=275')">Taiji</a> diagram, commonly known in the West as "the yin-yang symbol," came to represent yin and yang as well. This diagram illustrates the unity and interdependence of yin and yang within the Tao, with a yin dot in the yang side of the diagram and vice versa. It also represents the idea that yin energy begins to rise from its lowest level when yang is at its height. Likewise yang begins to rise when yin is at its height. This is most evident in the cyclical movements of the seasons: the first signs of spring begin to appear immediately after winter has peaked and begun to subside.</P>
