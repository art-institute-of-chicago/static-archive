<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Taoism traces its origins to <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a>, whose name literally means "elder master." Early historical sources indicate that Laozi was a scholar in the royal archives in the sixth century B.C.; however, these sources are probably based on legends. It is possible that Laozi may not have existed as a historical figure. The philosophical text attributed to him, the  <I> <A HREF="javascript:launchHelp('/taoism/glossary/classic.php', 'width=250,height=275')">Classic of the Way and Its Power</A>,</I> was compiled around the third century B.C., although some of its ideas may have been more than a century old by that time.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Later, in the second century A.D., Laozi was deified as the <A HREF="javascript:launchHelp('/taoism/glossary/celestial-worthy.php', 'width=250,height=275')">Celestial Worthy of the Way and Its Power</A>, one of the highest gods of the Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a>. He was seen as a direct embodiment of the Way itself. It is significant that <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</A> has no supreme being; each god in the pantheon merely gives a face to the endlessly changing Way.</P>
