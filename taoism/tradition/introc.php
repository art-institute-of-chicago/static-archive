<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>	

<P>The ancient Chinese believed that matter and energy (<I><A HREF="javascript:launchHelp('/taoism/glossary/qi.php', 'width=250,height=275')">qi</a></I>) were fundamentally the same. With their forms thrusting up toward the heavens, mountains were the most visible examples of energy converted into matter. As such they have always played an important role in the religious beliefs of the Chinese. Central to the worship of mountains was the belief that there were <A HREF="javascript:launchHelp('/taoism/glossary/five-sacred.php', 'width=250,height=275')">Five Sacred Peaks</a>, in the north, south, east, west, and center of China, that were directly linked to the heavens. Because of their special energy, mountains were believed to nurture the magical herbs and fungi used in <A HREF="javascript:launchHelp('/taoism/glossary/elixir.php', 'width=250,height=275')">elixirs</a> of immortality. They were also considered fitting places for meditation and spiritual retreat.</P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Mountains also served as the earthly homes of <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</a> and gods. One of the most famous early goddesses, the <A HREF="javascript:launchHelp('/taoism/glossary/queen-mother.php', 'width=250,height=275')">Queen Mother of the West</a>, was believed to dwell on a <A HREF="javascript:launchHelp('/taoism/glossary/sacred-mountain.php', 'width=250,height=275')">sacred mountain</A> called Kunlun to the far west of China. She was thought to have appeared to several emperors to teach them the arts of longevity and was worshiped by all levels of society as a divine <A HREF="javascript:launchHelp('/taoism/glossary/matriarch.php', 'width=250,height=275')">matriarch</a>. Other immortals were also associated with mountains. In fact, some of the most important Taoist temples devoted to these immortals were actually built on their sacred mountains. Worship of these immortals, including the Queen Mother of the West, continues in Chinese communities today.</P>
