<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>As the legends surrounding <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a> developed in the <A HREF="javascript:launchHelp('/taoism/glossary/han-dynasty.php', 'width=250,height=275')">Han dynasty</a>, he came to be seen as not only a human sage, but also a divine being who appeared again and again in the world to guide the development of Chinese civilization. He was first worshiped as a god at the Han court in the second century A.D. By the <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang dynasty</a>, he was seen as an imperial ancestor. At the same time, Laozi had enormous popular appeal, and the work attributed to him, the <I><A HREF="javascript:launchHelp('/taoism/glossary/classic.php', 'width=250,height=275')">Classic of the Way and Its Power</a>,</I> was widely read, even outside Taoist circles.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This painting is attributed to one of the most famous <A HREF="javascript:launchHelp('/taoism/glossary/chan.php', 'width=250,height=275')">Chan (Zen) Buddhist</a> painters, the monk Muqi. It presents Laozi not as the triumphant deity of <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a>, but as a world-weary scholar. </P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Works in the style of Muqi were actually more popular in Japan than in China. This scroll bears the <A HREF="javascript:launchHelp('/taoism/glossary/seal.php', 'width=250,height=275')">seal</a> of the 14th-century Japanese <A HREF="javascript:launchHelp('/taoism/glossary/shogun.php', 'width=250,height=275')">shogun</a> Ashikaga Yoshimitsu, which indicates that the painting had already traveled to Japan within a century of Muqi's death. Because Muqi's style was favored by the powerful shoguns, many Japanese painters immediately began to imitate it.</P>
