<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Tomb Tile with the Queen Mother of the West</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This tomb tile shows the <A HREF="javascript:launchHelp('/taoism/glossary/queen-mother.php', 'width=250,height=275')">Queen Mother of the West</a> in her palace on Mount Kunlun, a <A HREF="javascript:launchHelp('/taoism/glossary/sacred-mountain.php', 'width=250,height=275')">sacred mountain</A>. One of the most important goddesses of the Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a>, the Queen Mother of the West was believed to be the supreme <A HREF="javascript:launchHelp('/taoism/glossary/matriarch.php', 'width=250,height=275')">matriarch</a> who governed all other female deities. This tile comes from <A HREF="javascript:launchHelp('/taoism/glossary/sichuan.php', 'width=250,height=275')">Sichuan</a> province, heart of the <A HREF="javascript:launchHelp('/taoism/glossary/way-of-masters.php', 'width=250,height=275')">Way of the Celestial Masters</a>—the earliest religious movement to lay the foundation for <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a>. The tile dates to the very time that this movement was first coming to power. </P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Many early legends tell of mortals, especially rulers, who traveled to Mount Kunlun to meet with the Queen Mother of the West and to be entertained at feasts hosted by her. Since this tile was excavated from a tomb, the feast shown on it may represent the afterlife. As a feminine image, the Queen Mother of the West is associated with yin energy; the location of her palace on Mount Kunlun, toward the west, is also associated with yin. Here she is seated between a <A HREF="javascript:launchHelp('/taoism/glossary/tiger-and-dragon.php', 'width=250,height=275')">tiger and dragon</a>, symbols of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</a> that represent her transcendence of these two energies. To her left, a hare holds a candelabra while a toad dances in front of her. These animals symbolize the moon, yin counterpart to the yang sun. </P>
