<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Trigrams</CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><A HREF="javascript:launchHelp('/taoism/glossary/trigrams.php', 'width=250,height=275')">Trigrams</a> are symbols of the cycle of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</A> energy present in all things. Each of the Eight Trigrams consists of three horizontal lines that represent either yin or yang energy. Yang energy is depicted as a continuous line and yin energy by a broken line. Each trigram is read from bottom to top. The lowest line represents the earth; the center, humanity; and the top, heaven. The circular chart illustrates the relationship of the Eight Trigrams, each of which has its own name (li, kun, dui, etc.), with the cardinal directions, seasons, times of day, and elements (water, fire, heaven, earth, etc.). Each trigram also corresponds to phenomena not listed on the diagram, such as members of the family, numbers, colors, parts of the body, and illnesses.</P><CENTER>
