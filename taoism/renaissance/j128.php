<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Female Immortals</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Although Taoist <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</a> were commonly depicted as officials or scholars, they could also be shown as wild, even frightening, nature spirits who lived free of social norms. This is because Taoist practitioners often viewed social obligation as a hindrance to spiritual pursuits and were known to seek retreat in the mountains, far from civilization. These three female immortals walk on a remote mountain path. Barefoot, they are dressed in skirts made of leaves and jackets made of grass. One of them grasps a traveler's walking stick and two wear backpacks. The goddess on the right carries bananas, twigs, and cloth-wrapped bundles in her pack, while the one on the left carries a painted fan, a parasol with mushrooms, a double gourd, a skull, herbs, and tassels in her more elaborate pack. Many of these items have a symbolic significance. The medicinal mushrooms and herbs can be eaten for long life, and the double gourd, which represents the joining of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</a>, is frequently carried by Taoist immortals.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>These figures resemble early depictions of the female immortal Magu, and may, in fact, represent her and two companions. Magu, whose name means "Hemp Lady," is usually shown with fingernails like talons, her identifying <A HREF="javascript:launchHelp('/taoism/glossary/attribute.php', 'width=250,height=275')">attribute</A>. Her legend dates back to at least the fourth century A.D., and she is best known for her longevity. In later times, her image became popular on birthday presents for women.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This work includes an inscription that suggests it was painted by an artist named Sun Jue.</P>
