<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Zhenwu, Supreme Emperor of the Dark Heaven</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Small statues like this one, made for personal worship, were common in the <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming dynasty</a>, when veneration of <A HREF="javascript:launchHelp('/taoism/glossary/zhenwu.php', 'width=250,height=275')">Zhenwu</a> was at its height. They were usually made from porcelain, although more costly materials like bronze and gold were also used. This bronze statue is rare because it bears an inscription on the interior containing not only the name of the <A HREF="javascript:launchHelp('/taoism/glossary/patron.php', 'width=250,height=275')">patron</a>, but also that of the artist. The inscription also indicates that the patron for whom the statue was made was a follower of <A HREF="javascript:launchHelp('/taoism/glossary/buddhism.php', 'width=250,height=275')">Buddhism</a>, suggesting the extent to which worship of the Perfected Warrior had spread by the early 15th century.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The Perfected Warrior is identified by his long, unbound hair and bare feet, as well as by the armor beneath his robes. The armor underscores his role as a martial god, while the long hair and bare feet refer to the legend that he made a long spiritual retreat on Mount Wudang in <A HREF=javascript:launchWin('/taoism/map.php')>Hubei</a> province to practice <A HREF="javascript:launchHelp('/taoism/glossary/self-cultivation.php', 'width=250,height=275')">self-cultivation</a>, during which time he was free from social norms. As Zhenwu became increasingly popular, other gods were depicted in a similar manner, however, the inscription clearly identifies this as a statue of Zhenwu. The statue has an imposing formality far beyond its small size, which is energized by the wind-blown edges of the god's robe where it opens to reveal his armor.</P>
