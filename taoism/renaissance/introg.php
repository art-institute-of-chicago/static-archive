<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>	

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>From its very beginnings, <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a> has made a special point to distinguish itself from <A HREF="javascript:launchHelp('/taoism/glossary/popular-religion.php', 'width=250,height=275')">popular religion</a>, especially local cults that relied on blood sacrifice as the primary means of worship. At the same time, Taoism developed from popular religious beliefs and practices and has been influenced by different regional traditions throughout its history. Popular religion has been an important source of new gods, and the orthodox Taoist establishment has frequently turned to popular traditions to renew its own spiritual doctrines.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The relationship between Taoism and popular religion, in particular the incorporation of popular gods into the official Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a>, became increasingly subject to official rules and procedures in the <A HREF="javascript:launchHelp('/taoism/glossary/song-dynasty.php', 'width=250,height=275')">Song dynasty</a>. Absorption of a local deity into the official Taoist pantheon meant imperial recognition of the deity's followers, with the political security that this recognition entailed. Imperial recognition could also provide increased economic opportunity for cults that centered around merchants and guilds. After the Song dynasty, Taoism and popular traditions often maintained a mutually beneficial relationship. Taoism was able to increase its appeal and expand its pantheon by absorbing popular deities, while local cults were able to avoid persecution and reach a wider audience through the elevation of their gods to national status.</P>
