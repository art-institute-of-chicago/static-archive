<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Zhenwu, Supreme Emperor of the Dark Heaven</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Like the small statue <I><a href=javascript:launchWin('/taoism/renaissance/i105.php')>Zhenwu, Supreme Emperor of the Dark Heaven</a>,</I> this is an interesting example of a Taoist god depicted in a <A HREF="javascript:launchHelp('/taoism/glossary/buddhist.php', 'width=250,height=275')">Buddhist</a> context. This <A HREF="javascript:launchHelp('/taoism/glossary/rubbing.php', 'width=250,height=275')">rubbing</a>, taken from a stone <A HREF="javascript:launchHelp('/taoism/glossary/stele.php', 'width=250,height=275')"> stele</a> (now destroyed) in a Buddhist temple, shows the god descending over swirling waves on a black cloud. His robes and banner, decorated with the stars of the Northern Dipper (Big Dipper), are whipped by the wind, symbolizing his stormlike spiritual power. A bolt of lightning charges his sword. He stands on the back of a tortoise entwined by a snake, symbol of the direction north in the <A HREF="javascript:launchHelp('/taoism/glossary/five-phases.php', 'width=250,height=275')">Five Phase</a> system. According to legend, when the Perfected Warrior battled the demon kings of the north, the kings caused <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</a> energy to appear as giant tortoises and snakes. <A HREF="javascript:launchHelp('/taoism/glossary/zhenwu.php', 'width=250,height=275')">Zhenwu</a> then conquered these beasts by stepping on them. Early on, the symbol was used to represent him. Later <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming-dynasty</a> images of Zhenwu often show these two animals at his feet.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The inscription on the right indicates that the stele from which this rubbing was taken was a restoration of an earlier stele that had been rubbed beyond recognition by 1586. This suggests that visitors to the temple had been making rubbings of the god's image for many years.</P>
