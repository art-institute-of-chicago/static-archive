<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>	

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The Chinese word for landscape literally means "mountains and water," and the many geographical features of the natural world—its rocks and streams, valleys and peaks, rising and falling movements—were believed to be material embodiments of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</a> energy. As such, landscape paintings did not just depict the outer forms of nature, but were equally concerned with the movements of the energies that infuse the natural world with life. All of the patterns of nature, from the loftiest cliff face to the smallest rock and from violent ocean to intimate stream, were viewed as outward signs of the vital energy (<I><A HREF="javascript:launchHelp('/taoism/glossary/qi.php', 'width=250,height=275')">qi</a></I>) that formed the basis for all matter.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Of all the material embodiments of energy, mountains were the most impressive, with their massive twisting forms thrusting upward to the heavens. Mountain cults developed even before the formation of <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a>, and they remained the most important sacred places in Taoism. Mountains were home to revered <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</a>, Taoist temples and retreats, and the herbs and fungi that gave long life.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>A landscape painting may be connected to Taoism because it depicts a mythical <A HREF="javascript:launchHelp('/taoism/glossary/sacred-mountain.php', 'width=250,height=275')">sacred mountain</A> populated by immortals, like the western Mount Kunlun, home of the <A HREF="javascript:launchHelp('/taoism/glossary/queen-mother.php', 'width=250,height=275')">Queen Mother of the West</a>, or the eastern mountain-island <A HREF=javascript:launchWin('/taoism/renaissance/l149.php')>Fanghu</A>. It may also be connected to Taoism because it depicts a real mountain known for its Taoist temples. Many Taoist priests spent a great deal of time in the mountains and became accomplished landscape painters themselves.</P>
