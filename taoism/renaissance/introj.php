<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>	

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>While the highest gods of Taoism appeared spontaneously from the energies underlying all matter, it was also possible for a human being to reach such a state of spiritual purity that he or she was given a place in the <A HREF="javascript:launchHelp('/taoism/glossary/hierarchy.php', 'width=250,height=275')">hierarchy</a> of <A HREF="javascript:launchHelp('/taoism/glossary/celestial.php', 'width=250,height=275')">celestial</a> beings. In fact, this was the ultimate goal of most Taoist spiritual practices. Humans who became immortal were thus not only gods to be worshiped but also models whose lives were emulated by Taoist practitioners who hoped to become gods themselves.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Such <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</a> have a long history in Chinese thought that predates the establishment of <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a>. They are described as "perfected beings" well before the <A HREF="javascript:launchHelp('/taoism/glossary/han-dynasty.php', 'width=250,height=275')">Han dynasty</a>. The possibilities suggested by their ultimate spiritual perfection laid the foundation for the rise of magicians and religious specialists in the Han dynasty. The system of thought and belief developed by these early figures would form one of the cornerstones of Taoism.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>In later times, immortals became popular figures in drama, which was closely related to Taoist ritual and often drew on Taoist legends. The most famous were the <A HREF="javascript:launchHelp('/taoism/glossary/eight-immortals.php', 'width=250,height=275')">Eight Immortals</a>, a loosely connected group of <A HREF="javascript:launchHelp('/taoism/glossary/alchemist.php', 'width=250,height=275')">alchemists</a> and spiritual masters believed to be the <A HREF="javascript:launchHelp('/taoism/glossary/patriarch.php', 'width=250,height=275')">patriarchs</a> of the <A HREF="javascript:launchHelp('/taoism/glossary/complete.php', 'width=250,height=275')">Complete Realization sect</a>, one of the major sects from the <A HREF="javascript:launchHelp('/taoism/glossary/yuan-dynasty.php', 'width=250,height=275')">Yuan dynasty</a> onward. These and other immortals, some of whom predate the Han dynasty, continue to be worshiped today and remain popular subjects in the visual, performing, and literary arts.</P>
