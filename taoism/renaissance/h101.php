<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>The Dipper Mother</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Legend has it that many ages ago, a great queen vowed to give birth to children who would help to guide the movements of the <A HREF="javascript:launchHelp('/taoism/glossary/tao.php', 'width=250,height=275')">Tao</a>. One fine spring day, she disrobed and entered a pool to bathe. Suddenly, she felt "moved," and nine <A HREF="javascript:launchHelp('/taoism/glossary/lotus.php', 'width=250,height=275')">lotus</a> buds rose from the pond. The lotus, a symbol borrowed from <A HREF="javascript:launchHelp('/taoism/glossary/buddhism.php', 'width=250,height=275')">Buddhism</a>, signifies purity and spiritual enlightenment since it rises from the mud (representing the physical impurities of the world) to become a brilliant flower. Each of these lotus buds opened to reveal a star, including the seven stars of the Northern Dipper (Big Dipper), one of the most important constellations in Taoism. Subsequently, this queen was deified, becoming known as the "Dipper Mother."</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This porcelain sculpture of the Dipper Mother depicts her as a heavenly goddess holding the sun and moon in her upraised hands. Her remaining 16 hands grasp various ritual implements and weapons.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Like the <I><a href=javascript:launchWin('/taoism/renaissance/h97.php')>Saintly Mother, Heavenly Immortal of the Eastern Peak</a>,</I> the Dipper Mother rose to prominence in the <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming dynasty</a>. She is still worshiped today in special halls devoted to her at Taoist temples like the <A HREF="javascript:launchHelp('/taoism/glossary/white-cloud.php', 'width=250,height=275')">White Cloud Monastery</a>, head of the <A HREF="javascript:launchHelp('/taoism/glossary/complete.php', 'width=250,height=275')">Complete Realization sect</A> in <A HREF=javascript:launchWin('/taoism/map.php')>Beijing</a>.</P>
