<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Marshal Wen</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Marshal Wen began as a popular deity worshiped in the southern coastal areas of <A HREF=javascript:launchWin('/taoism/map.php')>Zhejiang</a> province. As Zhejiang is a hot, humid region in southern China known for its frequent plagues and epidemics, it is no surprise that Marshal Wen was primarily known as a god who fought against plague demons. Legend has it that his terrifying appearance—blue face and bright red lips and hair—resulted from a selfless act. He once drank enough poison to kill a whole community so that it would not be poured into the communal well. </P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Although the cult around Marshal Wen began as a small-scale, regional movement, it eventually spread along the coastal areas of Zhejiang and <A HREF=javascript:launchWin('/taoism/map.php')>Jiangsu</a> provinces and also to <A HREF="javascript:launchHelp('/taoism/glossary/sichuan.php', 'width=250,height=275')">Sichuan</a>. He gained status as a national deity of the orthodox Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a>, but his following never really expanded beyond these areas. He is still worshiped in southern coastal regions today, especially in <A HREF=javascript:launchWin('/taoism/map.php')>Taiwan</a>. Several factors aided the spread of Marshal Wen's cult, the most important being that the merchants from coastal Zhejiang looked to him as a protector spirit, thereby spreading his worship along trade routes. He was also worshiped by scholars and Taoist priests, both of whom had an impact on the development of his cult.</P>
