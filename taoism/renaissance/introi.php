<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Introduction</FONT></P></CENTER>	

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><A HREF="javascript:launchHelp('/taoism/glossary/zhenwu.php', 'width=250,height=275')">Zhenwu</a>, the "Perfected Warrior," began as an entwined tortoise and snake, the animal symbol of the north in the <A HREF="javascript:launchHelp('/taoism/glossary/five-phases.php', 'width=250,height=275')">Five Phase</a> system. This emblem was called the "Dark Warrior" (<I>xuanwu</I>) until the 11th century, when the name was changed to "Perfected Warrior" (<I>zhenwu</I>). From this time onward, Zhenwu assumed human form and rapidly became one of the most important deities in the Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a>. This was in no small part due to both his identity as a warrior god and his association with the north, the direction from which China was constantly threatened by neighboring people from central Asia. As a result, the Perfected Warrior eventually became known as a protector of the state and imperial family. Sponsorship of Zhenwu by the emperor reached its peak during the <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming dynasty</a>—especially during the reign of the third Ming emperor, who credited the god with helping him seize the throne. A temple to Zhenwu still stands in the northern quadrant of the Ming imperial palace, later used by the rulers of the <A HREF="javascript:launchHelp('/taoism/glossary/qing.php', 'width=250,height=275')">Qing dynasty</a>.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Due to the popularity of the Perfected Warrior, his worship spread beyond the confines of Taoism. He became an important member of the <A HREF="javascript:launchHelp('/taoism/glossary/buddhist.php', 'width=250,height=275')">Buddhist</a> pantheon as well. Zhenwu is still actively worshiped, and his central temple on Mount Wudang in <A HREF=javascript:launchWin('/taoism/map.php')>Hubei</a> province remains one of Taoism's most important sacred sites.</P>
