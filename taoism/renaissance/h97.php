<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Saintly Mother, Heavenly Immortal of the Eastern Peak</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This goddess, usually known as the Sovereign of the Clouds of Dawn, developed relatively late in Chinese history, coming to prominence only in the early <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming dynasty</a>. Almost immediately, she became one of the most popular deities of northern China. She is closely connected with Mount Tai in <A HREF=javascript:launchWin('/taoism/map.php')>Shandong</a> province, the easternmost of the <A HREF="javascript:launchHelp('/taoism/glossary/five-sacred.php', 'width=250,height=275')">Five Sacred Peaks</a>, and her main temple is located on that mountain. Worshiped by at least the third century B.C., Mount Tai was traditionally seen as the gateway to the afterlife. The god of Mount Tai was traditionally male, a figure of considerable severity who controlled the paths of the dead. The Sovereign of the Clouds of Dawn was believed to be his daughter. Unlike her father, she is a compassionate figure associated with life, especially childbirth. She is the yielding <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin</a> counterpart to her father's <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yang</a> sternness. </P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This painting, which probably served as an image of worship in a Taoist temple, shows the Sovereign of the Clouds of Dawn seated on a throne in her <A HREF="javascript:launchHelp('/taoism/glossary/celestial.php', 'width=250,height=275')">celestial</a> court, surrounded by female <A HREF="javascript:launchHelp('/taoism/glossary/attendant.php', 'width=250,height=275')">attendants</a> and holding a <A HREF="javascript:launchHelp('/taoism/glossary/tablet.php', 'width=250,height=275')">tablet</a>. She can be identified by the multiple <A HREF="javascript:launchHelp('/taoism/glossary/phoenix.php', 'width=250,height=275')">phoenixes</a> in her headdress. Both this goddess and the <A HREF="javascript:launchHelp('/taoism/glossary/queen-mother.php', 'width=250,height=275')">Queen Mother of the West</a> are usually adorned with images of the phoenix, but the Queen Mother of the West wears only a single phoenix on her head, while the Sovereign of the Clouds of Dawn typically wears three or more.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>This work will only be shown at The Art Institute of Chicago.</B></P>
