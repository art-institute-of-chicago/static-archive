<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>The Fanghu Isle of the Immortals</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Fanghu (literally, "square jar") is one of three mythical island homes of <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</a> traditionally thought to lie in the sea off the east coast of China. Fanghu was a common theme in Chinese painting, and this <A HREF="javascript:launchHelp('/taoism/glossary/hanging-scroll.php', 'width=250,height=275')">hanging scroll</a> depicting it is one of the finest. Belief in this island dates to at least the third century B.C., when the first emperor of China sent an expedition into the eastern sea in the hopes of making contact with beings who could teach him the secrets of immortality. This expedition remains one of the more tragic events in Chinese history: since immortals were believed to have eternal youth, the emperor sent an embassy of young boys and girls to communicate with them. None returned. Largely because of this event, Taoists came to believe that Fanghu and the other islands either lay beyond violent seas that prevented mortals from finding them or rested on the backs of great tortoises who were constantly in motion, so that the mountains had no permanent location.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Wang Yun depicted the mythical Fanghu rising from such an ocean. In this scroll, a precariously perched, oddly-shaped rock formation rises forcefully from surging waves. The other islands can be seen in the background through mist. The island is inhabited by immortals, whose red-and-green palaces with gold roofs resemble Taoist temples nestled in the folds of the rock. The rest of the mountain is an ideal landscape adorned with magical plants and trees, misty vapors, and mysterious caverns from which waterfalls descend. The inscription in the upper left by the artist indicates that this hanging scroll was painted for a Taoist named Helao and based on an older <A HREF="javascript:launchHelp('/taoism/glossary/song-dynasty.php', 'width=250,height=275')">Song-dynasty</a> composition.</P>
