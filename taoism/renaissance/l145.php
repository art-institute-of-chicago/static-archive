<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Mountains of the Immortals</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The style of this <A HREF="javascript:launchHelp('/taoism/glossary/handscroll.php', 'width=250,height=275')">handscroll</a> intentionally imitates much earlier landscape paintings of the <A HREF="javascript:launchHelp('/taoism/glossary/north-south-dynasties.php', 'width=250,height=275')">Northern- and Southern-dynasties</a> period. When China was under the control of the foreign <A HREF="javascript:launchHelp('/taoism/glossary/mongol.php', 'width=250,height=275')">Mongol</a> government, this style represented communion with a distinctively Chinese cultural heritage. The reference to an older painting style is strengthened by the use of bright mineral pigments to color the mountains. This highlights the conception of the mountain as the material form of vital energy, the same concept that allowed the minerals found in mountains to be used as ingredients in <A HREF="javascript:launchHelp('/taoism/glossary/elixir.php', 'width=250,height=275')">elixirs</a> of <A HREF="javascript:launchHelp('/taoism/glossary/alchemy.php', 'width=250,height=275')">alchemy</a>.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Chen Ruyan's work is a classic depiction of an <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals'</a> paradise. The visual journey through the painting begins with the Taoist temple nestled in the mountains at the right of the scroll. Further on, an immortal sits in a clearing next to a <A HREF="javascript:launchHelp('/taoism/glossary/zither.php', 'width=250,height=275')">zither</a> and magical fungi and watches a young <A HREF="javascript:launchHelp('/taoism/glossary/attendant.php', 'width=250,height=275')">attendant</a> dance with cranes, symbols of longevity. In another clearing to the left, two more immortals walk among <A HREF="javascript:launchHelp('/taoism/glossary/auspicious.php', 'width=250,height=275')">auspicious</a> animals and plants, while a third immortal rides above them on a crane.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Chen Ruyan was an associate of several famous <A HREF="javascript:launchHelp('/taoism/glossary/yuan-dynasty.php', 'width=250,height=275')">Yuan-dynasty</a> landscape painters known for their connections with Taoism, including Ni Zan (1306–1374), who inscribed this painting. Chen aided in the downfall of the Yuan government and served the new <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming dynasty</A> until he was executed for an unknown offense in 1371. At the beginning of the Ming dynasty, this was already his most famous work.
</P>
