<a name=wayof></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Way of the Celestial Masters&nbsp;&nbsp;</B></FONT> 
		the first formal Taoist religious organization, founded in the late <a href="#han">Han dynasty
		</a> by Taoist master <a href="#zhang">Zhang Daoling</a>, who claimed to have received teachings 
		from the deified <a href="#laozi">Laozi</a>. Members of the <a href="#celestial">Celestial</a> 
		Masters sect addressed the spiritual needs of the community. Communal rites were performed 
		regularly, especially during seasonal changes. The Celestial Masters sect was also responsible 
		for healing, which required the recording of misdeeds on a paper addressed to one of the 
		<a href="#threeofficials">Three Officials</a> (heaven, earth, or water). The movement remains 
		active in China to this day.
</P>
<a name=whitecloud></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>White Cloud Monastery (Baiyun Guan)&nbsp;&nbsp;</B></FONT> 
	one of the most famous Taoist monasteries in China. The temple, located in <a href="/taoism/map.php">
	Beijing</a>, was first built in the <a href="#tang">Tang dynasty</a> and assumed its present name 
	when it was rebuilt in 1394 during the <a href="#ming">Ming dynasty</a>.
</P>

<A NAME=Y></a>

<a name=yin></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>yin and yang&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		two opposing types of energy or contrasting forces. Yin is described as yielding, passive, negative, 
		dark, and female. Yang is dynamic, assertive, positive, light, and male. The two energies are 
		opposite and yet mutually dependent. Yin may become yang and vice versa, just as day becomes night, 
		cold becomes hot, and the reverse. The behavior of yin and yang describes the structure of any 
		event or thing. It may be said that their dynamic relationship describes the operation of the 
		<a href="#tao">Tao</a> in its cycles of creation, and that their alternating movement underlies 
		the structure of everything in the universe. The concept of yin and yang is conveyed by the 
		<a href="#tiger">tiger and dragon</a> and by the <a href="#taiji">Taiji</a> symbol (see also 
		<a href="javascript:launchWin('/taoism/diag-taiji.php')">Taiji diagram</a>).
</P>
<a name=yuan></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Yuan dynasty (1260–1368)&nbsp;&nbsp;</B></FONT> 
		a period of foreign occupation by the nomadic tribes of Mongolia. Europe's diplomatic and religious interest 
		in China grew during the Yuan dynasty, and missionaries arrived for the first time. Marco Polo 
		of Venice worked for 17 years in the service of Khubilai Khan, grandson of the <a href="#mongol">
		Mongol</a> conqueror Genghis Khan. Faced with discrimination by foreign rulers, the educated 
		Chinese recalled their past and turned their energies to art and culture, including theater (which 
		was influenced by Taoism), painting, and poetry. The Taoist <a href="#eightimmortals">Eight 
		Immortals</a> became popular, and the great Taoist temple with its extraordinary painted murals, 
		the Palace of Eternal Joy (Yongle Gong), was built.
</P>
<A NAME=Z></a>
<a name=zhang></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Zhang Daoling&nbsp;&nbsp;</B></FONT> 
		(also known as <a href="#celestial">Celestial</a> Master Zhang) Taoist leader, from the second 
		century A.D., who converted the philosophical ideas of Taoism into a <a href="#popular">popular 
		religion</a>. He is said to have written approximately 24 works of Taoist scripture, cured the 
		sick through incantation, and taught people to confess their wrong deeds. The image of Zhang 
		Daoling riding a tiger became popular for expelling insects, curing diseases, and protecting 
		the family from calamity.
</P>
<a name=zhenwu></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Zhenwu&nbsp;&nbsp;</B></FONT> 
		(Perfected Warrior) the Taoist god who, over many centuries, evolved from an ancient symbol of 
		the north (the entwined tortoise and snake) into a superhuman warrior god, adopted in the 
		<a href="#ming">Ming dynasty</a> as the protector of the imperial household  
</P>
<a name=zhou></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Zhou dynasty (</B></FONT><FONT FACE="Verdana,sans-serif"><B>c.</B></FONT><FONT COLOR=#807040><B> 1050–256 B.C.)&nbsp;&nbsp;</B></FONT> 
		the longest dynasty in Chinese history; also the great age of early Chinese philosophy: 
		Confucius and <a href="#laozi">Laozi</a> both date from this period.  The Zhou kings, having 
		conquered the <a href="#shang">Shang</a>, established a principle of governance called the 
		Mandate of Heaven, meaning that power was bestowed by Heaven and governance was determined by 
		moral quality.
</P>
<a name=zither></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>zither&nbsp;&nbsp;</B></FONT>	<I>(n)&nbsp;&nbsp;</I> 
		a stringed instrument having usually 30 to 40 strings over a shallow horizontal soundboard 
		and played with a pick and fingers
</P>
