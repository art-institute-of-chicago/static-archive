	<I>(n)&nbsp;&nbsp;</I> 
		red mercury ore, or mercuric sulfide, which was highly valued and used for its color and chemical 
		properties; a primary ingredient used in the Taoist <A HREF=#elixir>elixirs</A> of <A HREF=#outer>
