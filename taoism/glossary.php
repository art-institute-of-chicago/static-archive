<a name=buddhism></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Buddhism&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		major religion based on the teachings of Siddhartha Gautama, a sixth-century-B.C. prince who became 
		known as the Buddha, or Enlightened One. In its philosophical teachings, Buddhism seeks the 
		liberation of the individual from the suffering inherent in life. As Buddhism spread from India to 
		South and Southeast Asia and finally to China, many diverse forms of the religion developed.
</P>
