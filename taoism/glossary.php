<A NAME=F></a>
<a name=fivephases></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Five Phases&nbsp;&nbsp;</B></FONT>	<I>(n, pl)&nbsp;&nbsp;</I> 
		the relationship of nature's five elements (water, wood, fire, metal, and earth) to various 
		natural cycles and phenomena. In Taoism, each of the five elements corresponds to a time of day, 
		direction, and season. Movement from one phase to the next occurs in defined sequences. For 
		instance, water (night, north, winter) eventually becomes wood (morning, east, spring). The 
		Five Phase system also includes the 12 animals of the <a href="#chinesezodiac">Chinese zodiac</a> 
		(for example, the rat and pig are water signs). The movements of the Five Phases are rooted in 
		the cycles of <a href="#yin">yin and yang</a>.
</P>
<a name=fivesacred></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Five Sacred Peaks&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		five <a href="#sacred">sacred mountains</a> located along the five directions (north, south, 
		east, west, and center) that occupy powerful places in Taoist geography. The sacred mountains 
		are not actually single peaks; rather they are networks of peaks, cliffs, gorges, hills, ravines, 
		etc. To communicate with the deities on these mountains, emperors ordered the construction of 
		important Taoist temples on each peak. Taoists also believe that <a href="#immortals">immortals</a> 
		inhabit the Five Sacred Peaks. On their slopes grow the magical mushrooms that bestow immortality 
		(see <a href="javascript:launchWin('/taoism/map.php')">map</a>).
</P>
<A NAME=H></a>
<a name=han></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Han dynasty (206 B.C.–A.D. 220)&nbsp;&nbsp;</B></FONT> 
		the period of great cultural and territorial expansion that brought China into contact with 
		other cultures, including those of Southeast Asia and Korea. During this time, <a href="#confucius">
		Confucianism</a> began to flourish and <a href="#buddhism">Buddhism</a> was brought to China by 
		merchants along the <a href="#silk">Silk Roads</a>. In the Han dynasty, the civil service was 
		developed, the first history of China was written, and the first Chinese dictionary was compiled. 
		Many concepts that became the foundations for <a href="#religious">religious Taoism</a> (e.g., 
		notions of the cosmos and immortality) developed during this period, and <a href="#laozi">Laozi</a> 
		was deified. Taoist-inspired rebellions ultimately brought about the collapse of the dynasty. Many 
		of the art objects that survive from this period are burial objects that reflect ideas about 
		<a href="#cosmology">cosmology</a> and the afterlife.
</P>
<a name=hand></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>handscroll&nbsp;&nbsp;</B></FONT>  
	<I>(n)&nbsp;&nbsp;</I> 
		a painting or piece of calligraphy made in the form of a horizontal scroll specifically intended 
		for occasional, intimate viewing. Handscrolls are viewed just as Chinese is read: a section at a 
		time, from right to left. Handscrolls vary considerably in length; although some are quite short, 
		others may extend to over 70 feet in length (see <a href="javascript:launchWin('/taoism/diag-scrolls.php')">scroll diagram</a>).
</P>
<a name=hanging></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>hanging scroll&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a painting or piece of calligraphy made in the form of a vertical scroll hung either on a wall 
		or from the end of an <a href="#attendant">attendant</a>'s pole. Unlike oil canvases or panels, 
		scroll paintings could be easily taken down and replaced at various times of the year to suit the 
		tastes of visitors or to mark certain occasions, such as the changing seasons. Scroll paintings are 
		remounted every few decades to repair any damage and help preserve the image (see 
		<a href="javascript:launchWin('/taoism/diag-scrolls.php')">scroll diagram</a>).
</P>
