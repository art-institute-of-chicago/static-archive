<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>immortals&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		in Taoism, individuals who have achieved eternal life through perfect realization of the 
		<a href="#tao">Tao</a>. One may become immortal through meditation or <a href="#innervis">Inner 
		Visualization</a>, physical training and breathing techniques, the ingestion of <a href="#elixir">
		elixirs</a>, or moral behavior. Taoists believe that immortals dwell in the heavens, in caverns, 
		on mountains, and in other magical paradises.
</P>
