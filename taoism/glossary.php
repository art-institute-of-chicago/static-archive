<A NAME=L></a>
<a name=laozi></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Laozi&nbsp;&nbsp;</B></FONT> 
		literally, "old master," traditionally assumed to have been born in the sixth century B.C. He 
		is considered the author of the earliest Taoist philosophical text, the <I>		<a href="#classic">Classic of the Way and Its Power (Daode jing)</a>.</I> 
		Historians now agree that Laozi was a legendary figure developed to provide an author for the 
		<I>Daode jing</I>, which was compiled by a group 
		of scholars in the third century B.C. During the <a href="#han">Han dynasty</a>, Laozi was deified; 
		he remains one of the most important deities in <a href="#religious">religious Taoism</a>.
</P>
<a name=lotus></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Lotus&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a plant of the water-lily family that grows in water or mud. Because its blossoms emerge pure 
		and beautiful out of muddy waters, <a href="#buddhist">Buddhists</a> view the lotus as a symbol 
		of human beings' true nature, which can remain unstained by the mud of the world. The lotus may 
