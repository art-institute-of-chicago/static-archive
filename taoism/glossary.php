</P>
<a name=celestialworthy></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Celestial Worthy of the Way and its Power&nbsp;&nbsp;</B></FONT>		divine title of <a href="#laozi">Laozi</a> by the second century A.D. In this guise, Laozi is one of the 
		<a href="#threepurities">Three Purities</a>, the greatest Taoist gods. The name is derived from the Taoist 
