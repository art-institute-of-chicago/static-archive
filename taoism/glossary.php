		things, as well as the two complementary forces of <a href="#yin">yin and yang</a>. To 
		realize the Tao, one must live simply and virtuously, in harmony with nature. When understood 
		or discovered, the Tao provides strength and can lead to immortality. The Tao became the central 
		focus and principle of <a href="#religious">religious Taoism</a>.
