<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Tang dynasty (618–906)&nbsp;&nbsp;</B></FONT> 
	at its height, Tang China was the largest and most powerful empire in the world.  During the 
	<a href="#sui">Sui</a> and Tang dynasties, a professional civil-service bureaucracy rose to 
	prominence. Tang power and influence was felt throughout Asia and even the Middle East. The 
	blend of new and foreign ideas contributed to a rich mixture of tradition and cultural fruition. 
	Many ideas from the West made their way into China along the <a href="#silk">Silk Roads</a> during 
	this period. <a href="#buddhism">Buddhism</a> was the strongest foreign influence.  When China 
	began to suffer from foreign invasion toward the end of the Tang, Buddhism suffered imperial 
	persecution.  Taoism, however, flourished during this period and gained much imperial support.  
