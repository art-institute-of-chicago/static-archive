<a name=threeofficials></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Three Officials&nbsp;&nbsp;</B></FONT> 
	a triad of Taoist deities in charge of heaven, earth, and the waters under the earth. The Three 
	Officials record people's good and bad deeds and determine their life span and destiny.
</P>
<a name=threepurities></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Three Purities (Three Clarities)&nbsp;&nbsp;</B></FONT> 
	the highest deities in Taoism, they reside over the three greatest heavenly realms. Their names are 
	the <a href="#celestial">Celestial</a> Worthy of <a href="#primordial">Primordial</a> Beginning, the 
	Celestial Worthy of <a href="#numinous">Numinous</a> Treasure, and the <a href="#celestialworthy">
	Celestial Worthy of the Way and Its Power</a>.
</P>
<a name=tiger></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>tiger and dragon&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		traditional symbols of <a href="#yin">yin</a> (tiger) and <a href="#yin">yang</a> (dragon).
		The <a href="#taiji">Taiji</a> symbol, introduced during the <A HREF=#song>Song dynasty</A>, 
		eventually surpassed the image of the tiger and dragon as the most commonly recognized visual 
		emblem of yin and yang.
</P>
<a name=trigrams></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>trigrams (Eight Trigrams)&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		symbols of the cycle of <a href="#yin">yin and yang</a> energy present in all things. Each of 
		the Eight Trigrams consists of three horizontal lines that represent either yin or yang energy. 
		Yang energy is depicted as a continuous line, and yin energy by a broken line. Each of the 
		trigrams embodies a particular configuration of yin and yang, ranging from completely yang, with
		three unbroken lines, to completely yin, with three broken lines. The Eight Trigrams can appear 
		in different orders, arranged in a circle (see <a href="javascript:launchWin('/taoism/diag-trigram.php')">trigram diagram</a>). 
		Not specific to Taoism, the Eight Trigrams were absorbed into Taoism as it became an organized 
		religion.
</P>
<a name=triptych></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>triptych&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a work of art composed of three panels or parts, usually a center section and one wing on 
		each side. The three sections often share one common subject or theme.
</P>

