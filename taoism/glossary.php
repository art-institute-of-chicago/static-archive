</P>
<a name=classic></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Classic of the Way and Its Power (<I>Daode jing</I>) (also spelled <I><a href="#tao">TAO</A> TE CHING</I>)
	</B></FONT> 
		the earliest-known text of the Taoist tradition, which is said to have been authored by the 
		legendary figure <a href="#laozi">Laozi</a>. The text is actually a compilation of various 
		writings collected over the course of generations. It may have assumed its current form by the 
		third or fourth century B.C. The <I>Classic of the Way and Its Power
		</I> includes poetic passages, sayings, fragments of political texts, and 
		passages intended for recitation. It served as the foundation for both philosophical and 
		<a href="#religious">religious Taoism</a>.
</P>
<a name=complete></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Complete Realization sect&nbsp;&nbsp;</B></FONT>	<I>(n)&nbsp;&nbsp;</I> 
		a Taoist monastic order founded in northern China around 1160. The sect combines the teachings 
		of Taoism, <a href="#buddhism">Buddhism</a>, and <a href="#confucius">Confucianism</a>. The 
		goal of the sect's followers was to attain immortality by perfectly realizing the <a href="#tao">
		Tao</a> in themselves. Both male and female members of this sect practiced a strict monastic 
		lifestyle. It survives today as one of the two major sects of Taoism, and its headquarters is 
		the <a href="#whitecloud">White Cloud Monastery</a> in <a href="javascript:launchWin('/taoism/map.php')">Beijing</a>.
</P>
<a name=confucius></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Confucianism&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a philosophical practice based on the teachings of Confucius (c. 550–478 B.C.), a Chinese scholar 
		and teacher. Confucianism offered a model of government that emphasized a ruler's moral qualities, 
		such as the correct and harmonious fulfillment of his family relations. The relationship of father 
		and son provided the model for all relationships, including that between the ruler and his 
		<a href="#minister">ministers</a>. Later Confucianism of the <a href="#song">Song dynasty</a> 
		(called Neo-Confucianism by Western scholars) added a <a href="#cosmology">cosmological</a> 
		dimension that drew on the principles of Taoism and <a href="#buddhism">Buddhism</a>.
</P>
<a name=contrition></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>contrition&nbsp;&nbsp;</B></FONT>	<I>(n)&nbsp;&nbsp;</I> 
		state of guilt or remorse resulting from wrong or evil actions
</P>
<a name=cosmology></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>cosmology; cosmological&nbsp;&nbsp;</B></FONT>	<I>(n; adj)&nbsp;&nbsp;</I> 
		beliefs about the origin and structure of the universe. Chinese cosmology referred not only 
		to the structure and operation of the heavens, but also to that of the earth and human beings. 
		Cosmology in this context implies the way that these realms work together and affect the others; 
		of or relating to the origin and structure of the universe
</P>
