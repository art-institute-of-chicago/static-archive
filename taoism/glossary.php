<a name=chan></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Chan (Zen) Buddhist&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		one who practices the type of <a href="#buddhism">Buddhism</a> that developed in China in the 
		sixth and seventh centuries. This type of Buddhism combined the doctrines of Indian Buddhism 
		with Chinese ideas, including some based on the <I><a href="#classic">
		Classic of the Way and Its Power</A>.</I> More than any other type of 
		Buddhism, Chan stresses the importance of the enlightenment experience and the uselessness of 
		rituals while encouraging intellectual analysis of the doctrine.
</P>
<a name=chinesezodiac></a>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Chinese zodiac&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		organization of the calendar into 12-year cycles, each represented by an animal associated with 
		specific personality traits. The animals include: rat, ox, tiger, hare, dragon, snake, horse, ram, 
		monkey, cock, dog, and pig. People born in the zodiac year of a specific animal are said to possess 
		that animal's traits. See the <a href=javascript:launchWin('/taoism/diag-zodiac.php')>Chinese zodiac diagram</a> for a 
		year-by-year list of each sign's particular characteristics.
</P>
<a name=cinnabar></a>
