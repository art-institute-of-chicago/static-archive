<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Lesson Objective:</B> Students will imagine what it would be like to enter a landscape painting and then write a poem on nature.</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Related Objects:</B> <a
href=javascript:launchWin('/taoism/renaissance/l145.php')><I>Mountains of the Immortals</A>, 
<a
href=javascript:launchWin('/taoism/renaissance/l149.php')>The Fanghu Isle of the Immortals</I></A></FONT></P> 

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Subject Area:</B> Language Arts<BR>
<B>Suggested Grade Levels:</B> Middle and Secondary School</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Instructional Materials:</B></FONT></P>	
<UL CLASS=BodySmall><LI>Descriptive texts on <I>Mountains of the Immortals</I> and <I>The Fanghu Isle of the Immortals</I><LI>"Mountains and Landscape" section from <a
href=javascript:launchWin('/taoism/books.php#journey')><I>Taoism and the Arts of China: The Journey to Perfect Harmony</I></A>
<LI>Encyclopedia</UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>Read the information on <I>Mountains of the Immortals</I> and <I>The Fanghu Isle of the Immortals</I> and have students identify these elements of a Chinese landscape: mountains, trees, waterfalls, clouds, a Taoist temple, <A HREF="javascript:launchHelp('/taoism/glossary/immortals.php', 'width=250,height=275')">immortals</A>, and a crane.
<LI>As you locate these details, discuss their possible symbolic significance. 
<LI>Ask students to determine whether these scenes are taking place in an imagined or actual location. 
<LI>Discuss the Taoist concept of an immortals' paradise. Compare the two images to Western art by discussing their composition, perspective, colors, material, and format.</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Secondary School:</FONT></P><UL CLASS=BodySmall><LI>Follow the procedure listed above to guide classroom discussion on the two Chinese landscape paintings. Ask students to read the poem below by Li Po, a famous <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang-dynasty</a> poet.<BR><BR>
<UL CLASS=BodySmall><I>Dialogue in the Mountains</I><BR>
You ask me why I lodge in these emerald hills;<BR>
I laugh, don't answer—my heart is at peace.<BR>
Peach blossoms and flowing waters<BR>
go off to mysterious dark,<BR>
And there is another world,<BR>
not of mortal men.<BR>
—Li Po*</UL></UL>
<UL CLASS=BodySmall><LI>Discuss the kind of landscape Li Po is describing. Suggested questions include:<BR>
—Is it similar to the scenes in the landscape paintings?<BR>
—What does Li Po mean by the second line? Why is his heart "at peace"?<BR> 
—What might be "mysterious" and "dark" in the landscape Li Po is describing?<BR> 
—Discuss the other world that is "not of mortal men."
</UL> 
<UL CLASS=BodySmall><LI>Keeping these questions in mind, students will write down their interpretations of this poem and share them with the class. Encourage students by telling them that poetry, like art, is subjective and therefore open to interpretation.</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>*Poem excerpted from Stephen Owen, <I>The Great Age of Chinese Poetry: The High Tang,</I> page 136</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>Ask students to choose one of the above paintings and make a list of its details. 
<LI>Encourage them to imagine entering the landscape, thinking about the following questions:<BR>
—What natural and man-made things do you see?<BR> 
—What would you like to explore?<BR> 
—How does it feel to be standing under the soaring peaks?<BR>
—What sounds, smells, and textures surround you?<BR> 
—Is anyone else with you in the landscape?
</UL><UL CLASS=BodySmall><LI>Keeping their imaginings in mind, students should write a poem based on their responses to the questions above and including the details they noted earlier. Instruct them to use adjectives to describe the place. 
<LI>Encourage students to use similes to more accurately describe the landscape.</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Secondary School:</FONT></P><UL CLASS=BodySmall><LI>Complete the poetry-writing activity listed above.</UL>
