<B>Suggested Grade Level:</B> Secondary School</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Instructional Materials:</B></FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Mirror with Animals of the Four Directions and the Zodiac</I><LI><a
href=javascript:launchWin('/taoism/books.php#artsofasia')><I>The Arts of Asia: China, Korea, Japan</A></I><LI>Compass
<LI>Encyclopedia
