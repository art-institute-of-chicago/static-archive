</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Suggested Books:</FONT></P><UL CLASS=BodySmall><LI><I><a
href=javascript:launchWin('/taoism/books.php#oracle')>Oracle Bones, Stars, and Wheelbarrows: Ancient Chinese Science and Technology</A></I><LI><I><a
href=javascript:launchWin('/taoism/books.php#madeinchina')>Made in China: Ideas and Inventions from Ancient China</A></I></UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P><UL CLASS=BodySmall><LI>Read the information on <I>Mirror with Animals of the Four Directions and the Zodiac</I> and discuss the four animals that represent the cardinal directions of the compass. According to ancient Chinese tradition, each direction was believed to have magical qualities and was aligned with a specific animal. 
<LI>Help the class identify these animals using the descriptive information and the image. 
<LI>Ask students to find out how bronze was used in ancient China by researching the history of bronze casting in the ancient civilizations of China and India. 
<LI>Discuss the texture of bronze objects, such as the mirror. Encourage students to imagine how bronze might feel.
<LI>Resuming the discussion of the ancient Chinese perception of the four directions, explain that the compass was an ancient Chinese invention. Discuss the use of a compass and use one to locate the four directions in your classroom. Challenge students to find out how it works.</UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P><UL CLASS=BodySmall><LI>Divide the class into groups and have them research other inventions made by the ancient Chinese, such as porcelain, gunpowder, silk, and the seismograph. Create a list on the blackboard. 
<LI>Assign one invention to each group, instructing them to find out how it was created, what it was used for, and if it is still used. 
<LI>When all of the groups have finished, create a visual chart of ancient Chinese inventions, using words and images. Include the date of each invention as well as a variety of two- and three-dimensional works and time arts to articulate ideas.
<LI>Ask each group of students to present their findings to the other groups.</UL>
