<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Lesson Objective:</B> Students will create a miniature Chinese robe made of paper.</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Related Object: </B>     <I><a
href=javascript:launchWin('/taoism/church/e52.php')>Taoist Priest's Robe</a></I></FONT></P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Subject Area:</B> Fine Arts<BR>
<B>Suggested Grade Levels:</B> Elementary and Middle School</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Instructional Materials:</B></FONT></P> 

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Elementary School:</FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Taoist Priest's Robe</I><LI>White 8.5"x 11" paper
<LI>Colored markers
<LI>Pencils
<LI>Erasers</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Taoist Priest's Robe</I><LI>White 11"x 18" paper
<LI>Rulers
<LI>Colored construction paper
<LI>Scissors
<LI>Tempera paints and brushes
<LI>Water cups
<LI>Paper towels
<LI>Mixing trays
<LI>Colored pencils
<LI>Colored markers
<LI>Glue</UL>

<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P>
<UL CLASS=BodySmall><LI>Read the information on the <I>Taoist Priest's Robe</I> and share it with your students as they look carefully at the online reproduction.
<LI>Help the class identify some of the images on the robe, such as the mountains at the bottom, the <A HREF="javascript:launchHelp('/taoism/glossary/pagoda.php', 'width=250,height=275')">pagoda</A> in the center, the cranes in each of the five circles, and the numerous snakelike dragons.
<LI>Discuss the following elements of style and form: colors (red and gold), shapes (numerous circles), and lines (both curved and straight). 
<LI>Discuss with the class the function of the robe (once worn by a priest in a Chinese temple).</UL> 

<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Elementary School:</FONT></P><UL CLASS=BodySmall><LI>Use a printout of the <I>Taoist Priest's Robe</I> to trace an outline of its shape and create a template. Students can also begin by using pencils to execute their own freehand drawings of the robe.
<LI>Students should then be encouraged to select a dominant background color and then to decorate the background with an arrangement of geometric and organic shapes and realistic or <A HREF="javascript:launchHelp('/taoism/glossary/stylized.php', 'width=250,height=275')">stylized</A> images.</UL> 

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>The dimensions of the actual robe are 49 5/8" x 73 3/8". Using a scale of 5"=1", challenge students to calculate the length and height of a miniature robe by dividing the actual dimensions by 5.
<LI>Give each student two pieces of white paper to create the miniature robe. Using the scaled-down length and height, have students use one piece of paper to create the front of the robe and another to create the back. 
<LI>Ask students to use tempera paint to give their robes a background color. 
While the robes dry, ask students to recall the shapes and images on the <I>Taoist Priest's Robe</I> and choose those they would like to reproduce on their miniature robes. Students will then cut these shapes out of different colored construction paper. 
<LI>When the paint dries, they can glue the front and back of their miniature robe together at the top and the sides. Then students can glue the cut-out motifs onto the front and the back of their robes. Using colored pencils and markers, students should add final touches to the front and back of their robes.</UL>
