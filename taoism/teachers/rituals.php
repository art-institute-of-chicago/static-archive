<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P><UL CLASS=BodySmall><LI>Have the class read the information on the <I>Taoist Priest's Robe</I> and then discuss the images on the robe, including the <A HREF="javascript:launchHelp('/taoism/glossary/five-sacred.php', 'width=250,height=275')">Five Sacred Peaks</A>, the <A HREF="javascript:launchHelp('/taoism/glossary/trigrams.php', 'width=250,height=275')">Eight Trigrams</a>, and the cranes. 
<LI>Discuss the robe's original function and the types of rituals in Taoism. 
<LI>Discuss the difference between daily rituals and religious or cultural rituals. Encourage students to talk about rituals they have observed in their communities. 
<LI>Discuss rituals practiced by people throughout the United States.</UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P> 
<UL CLASS=BodySmall><LI>Divide the class into groups and assign each group a particular world religion or culture. Each group should research a ritual from their assigned religion or culture. 
<LI>Ask students to think about how their particular ritual is representative of that religion or ethnic group. 
<LI>Each group will create a visual display incorporating words and images that depict their selected ritual. Ask them to include a variety of two-and three-dimensional works and time arts to articulate their ideas. If possible, encourage them to bring in ritual implements and other objects used in the ritual. 
<LI>When students have completed their research projects, ask each group to present its findings to the class.</UL>	
