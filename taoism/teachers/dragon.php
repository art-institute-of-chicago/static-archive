<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Lesson Objective:</B> Students will compare the symbolism and legends of dragons in China with those of other countries and cultures. They will then write a magazine article comparing dragons in Western and Chinese cultures.</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Related Object: </B> <a
href=javascript:launchWin('/taoism/tradition/b14.php')><I>Mirror with Animals of the Four Directions and the Zodiac</I></A></FONT></P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Subject Area:</B> Language Arts<BR>
<B>Suggested Grade Level:</B> Middle School</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Instructional Materials:</B></FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Mirror with Animals of the Four Directions and the Zodiac</I><LI>Pencil and paper</UL><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Suggested Books: </FONT></P><UL CLASS=BodySmall><LI><a
href=javascript:launchWin('/taoism/books.php#bookofdragon')><I>The Book of the Dragon</I></A>
<LI><a
href=javascript:launchWin('/taoism/books.php#dragongods')><I>Dragon Gods and Spirits from Chinese Mythology</I></A>
</UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P><UL CLASS=BodySmall><LI>Read the information on <I>Mirror with Animals of the Four Directions and the Zodiac</I> and have students identify the different animals depicted on it. Clarify the difference between the larger dragon (symbolizing the east) in the center and the tiny dragon on the edge of the mirror, one of 12 animals symbolizing the <a
href=javascript:launchWin('/taoism/diag-zodiac.php')>Chinese zodiac</A>.  
<LI>Discuss the difference between the perception of dragons in Western culture (evil and destructive) and that in China, where only when angered do they cause destruction, such as storms and floods. Otherwise, dragons provide the rain that keeps the earth green and allows crops to grow. 
<LI>Locate stories about dragons in China and elsewhere and ask students to read them silently or aloud to each other.
<LI>Compare the Chinese dragon stories with the European legend of St. George and the Dragon, represented in a 15th-century painting by Bernardo Martorell in the Art Institute's collection.*
<LI>Discuss the following questions:
<UL CLASS=BodySmall><BR>
<LI>Did dragons really exist in the past? If not, why did people invent these mythical creatures?
<LI>What animals in the earth's history looked similar to dragons? Research two or three examples.
</UL></UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P><UL CLASS=BodySmall><LI>Have students write a magazine article comparing dragons from China with those from the West.</UL><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>*Additional information about and a reproduction of this image are available through the <a
