<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Lesson Objective:</B> Students will create a landscape painting using traditional Chinese techniques.</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Related Objects:</B> <a
href=javascript:launchWin('/taoism/renaissance/l145.php')><I>Mountains of the Immortals</A>, 
<a
href=javascript:launchWin('/taoism/renaissance/l149.php')>The Fanghu Isle of the Immortals</A></I></FONT></P> 

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Subject Area:</B> Fine Arts<BR>
<B>Suggested Grade Levels:</B> Middle and Secondary School</FONT></P>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Instructional Materials:</B></FONT></P>	
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Mountains of the Immortals</I> and <I>The Fanghu Isle of the Immortals</I><LI>White construction paper (8.5"x 11")
<LI>One long piece of white paper at least 8.5" wide, such as shelf paper
<LI>Black ink
<LI>Gold tempera paint and brushes 
<LI>Pencils and erasers
<LI>Plastic cups to hold water
<LI>Mixing trays
<LI>Glue
<LI>Two wooden dowels	
<LI>String</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Secondary School:</FONT></P><UL CLASS=BodySmall><LI>Descriptive text on <I>Mountains of the Immortals</I> and <I>The Fanghu Isle of the Immortals</I><LI>Writing sheets for the Chinese characters <A HREF="javascript:launchHelp('/taoism/glossary/shan.php', 'width=345,height=175')">mountain</A> and <A HREF="javascript:launchHelp('/taoism/glossary/shui.php', 'width=345,height=175')">water</A> 
<LI>Construction paper (12"x 18")
<LI>Black, red, and gold tempera paint and brushes
<LI>Plastic cups to hold water
<LI>Mixing trays
<LI>Glue
<LI>Scissors
<LI>Two wooden dowels	
<LI>String
<LI>Craft paper
<LI>Pencils and erasers
<LI>Potatoes, cut in half
<LI>X-Acto knives</UL>
<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Discussion:</B></FONT></P><UL CLASS=BodySmall><LI>The Chinese word for landscape is <I>shanshui</I> (pronounced "shan shway"), meaning "mountain and water." Ask students to identify the elements of the Chinese landscape. 
<LI>Share the information on the two Chinese landscape paintings with your students. Compare the two images and then discuss how the composition, perspective, colors, material, and format compare to Western art.
<LI>Ask students to define the word "landscape," considering how natural elements contribute to that definition.</UL> 

<HR SIZE='1' NOSHADE CLASS=ruleleft><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Activity:</B></FONT></P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Middle School:</FONT></P><UL CLASS=BodySmall><LI>Ask students to paint a landscape on a horizontal piece of white construction paper. Start with gold paint diluted in water as a wash. While it dries, students can create three different gradations of gray using the black ink. With the black and grays, they can add the details of the landscape.
<LI>After the paintings are dry, have each student write the poem they wrote for the lesson plan <a
href=javascript:launchWin('/taoism/teachers/poem.php')>A Poem for Nature</A> onto his or her painting. <I>(Tip: Have them write out a draft of their poem on a piece of paper. Then, instruct them to copy it onto their painting in their best handwriting.)</I><LI>Create a class <A HREF="javascript:launchHelp('/taoism/glossary/handscroll.php', 'width=250,height=275')">handscroll</A> by collecting all of the landscapes and attaching them to a long piece of paper. 
<LI>Leave five inches on either end of the paper for the wooden dowels. Since handscrolls are read from right to left, roll the scroll around the left dowel. Attach a piece of string to the right side of the long piece of paper. 
<LI>Place the rolled handscroll on a long table; as small groups of students unroll it, view the immense landscape. Hang it up in the classroom across a long wall.</UL>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Secondary School:</FONT></P><UL CLASS=BodySmall><LI>Ask students to paint a landscape on a horizontal piece of white construction paper. Start with gold paint diluted in water as a wash. 
<LI>While it dries, students can practice writing the Chinese characters for <A HREF="javascript:launchHelp('/taoism/glossary/shan.php', 'width=345,height=175')">mountain</A> and <A HREF="javascript:launchHelp('/taoism/glossary/shui.php', 'width=345,height=175')">water</A>, following the brush stroke order that appears on the diagram. 
<LI>Next, ask them to create three different gradations of gray using black tempera paint. 
<LI>When the gold paint has dried, students will paint a landscape using the Chinese characters for mountain and water and the different grays they created. They should leave a two-inch border on the right- and left-hand sides and a three-inch border at the top and bottom. 
<LI>Ask students to cut out pieces of craft paper and paste them to the edges of their paintings.
<LI>In China, a person's signature is often stamped with a personal <A HREF="javascript:launchHelp('/taoism/glossary/seal.php', 'width=250,height=275')">seal</A>. Students can make their own seal with a potato. Using X-Acto knives, students will create their own seal designs. Remind them to cut away the flat area around the design so that it stands out from the background. 
<LI>When the students are ready, tell them to press the seal into red tempera paint and try printing it on a scrap of paper. Then, have them stamp it onto their paintings.
<LI>Next, students should attach a dowel to the top and bottom of their paintings. Using glue, wrap the edges of the craft paper around the dowels. The length of the dowels should be longer than the width of the construction paper. 
<LI>Tie the string to the ends of the dowel to hang the scroll.</UL>
