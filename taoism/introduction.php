<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>Overview</B><BR>The exhibition <I>Taoism and the Arts of China</I> is on view at The Art Institute of Chicago from November 4, 2000, to January 7, 2001, and at the Asian Art Museum of San Francisco from February 21 to May 13, 2001. This is the first major exhibition of Taoist art in the United States, showcasing 151 works of art illustrating many facets of the Taoist religion. The exhibition includes paintings, calligraphy, sculpture, porcelain, lacquer, and ritual robes and implements from museums and private collections in the United States, Europe, Japan, China, Taiwan, and Hong Kong. These items date from the <a href=javascript:launchWin('/taoism/timeline.php')>Warring States period</a> to the <a href=javascript:launchWin('/taoism/timeline.php')>Qing dynasty</A> and demonstrate the development of Taoism and Taoist art from its earliest precedents to its "renaissance" in the late imperial age.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Admission to the exhibition is free with museum admission. The Art Institute will publish a <a href=javascript:launchWin('http://www.artic.edu/aic/books/subtaoism.html')>catalogue of the exhibition</a>, with essays by prominent scholars of Taoism and Chinese art history, and sponsor a symposium in the museum's Rubloff Auditorium on December 2 and 3, 2000. There will also be public <a href=javascript:launchWin('/taoism/calendar.php')>lectures and other educational events</a> relating to Taoism during the run of the exhibition. Families visiting the exhibition with children should pick up the free Family Self-Guide at the exhibition entrance or <A HREF="/taoism/images/FamilySelfGuide.pdf" TARGET="_blank" NAME="Family Self-Guide">download</A> it as a .pdf file. You will need <A HREF="http://www.adobe.com/products/acrobat/readstep.html" TARGET="_blank">Adobe Acrobat Reader</A> to read and print the document.  This site provides an overview of the exhibition using 28 works that address the themes of <I>Taoism and the Arts of China.</I></P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1><B>What Is Taoism?</B><BR>The name "Taoism" comes from the Chinese word <A HREF="javascript:launchHelp('/taoism/glossary/tao.php', 'width=250,height=275')">Tao</a> (pronounced "dow"), which literally means "way" or "path." The Tao is often translated as "the Way of all things," and is conceived of as an empty void pregnant with the infinite possibilities of existence. The Tao is beyond the capacity of words to describe and beyond all opposites. As a result, it is often described using intentionally paradoxical language:</P>
<P CLASS=Extract>The Way is like an empty vessel<br>
That yet may be drawn from<br>
Without ever needing to be filled.<br>
It is bottomless; the very progenitor of all things in the world.<br>
(Chapter four of the <I>Daode jing;</I> translation by Arthur Waley)</P>
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The philosophy of early texts like the <I><A HREF="javascript:launchHelp('/taoism/glossary/classic.php', 'width=250,height=275')">Classic of the Way and Its Power (Daode jing)</a>,</I> quoted above, eventually developed into China's primary indigenous religion, called the "Teachings of the Way" or Taoism, which still exists today. Although Taoism has been persecuted in mainland China in the recent past, it has experienced a resurgence in popularity as the government has taken a more lenient stance toward religion. Taoist temples and practitioners can now be found throughout China. <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">Religious Taoism</a> is also practiced all over the world, from Taiwan and Indonesia to Europe and the United States. Although religious Taoism has only recently been recognized in the West, many Taoist texts, like the <I>Classic of the Way and Its Power,</I> have been translated into Western languages and have served as sources of inspiration for Western thinkers and artists.</P>
