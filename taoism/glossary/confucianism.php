<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Confucianism&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a philosophical practice based on the teachings of Confucius (c. 550–478 B.C.), a Chinese scholar 
		and teacher. Confucianism offered a model of government that emphasized a ruler's moral qualities, 
		such as the correct and harmonious fulfillment of his family relations. The relationship of father 
		and son provided the model for all relationships, including that between the ruler and his 
		ministers. Later Confucianism of the Song dynasty (called Neo-Confucianism by Western scholars) 
		added a cosmological dimension that drew on the principles of Taoism and Buddhism.
