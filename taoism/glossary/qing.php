<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Qing dynasty (1644–1911)&nbsp;&nbsp;</B></FONT> 
		period during which the Manchus, foreigners from central Asia, took advantage of Ming imperial 
		weakness and successfully occupied China. During this period, China became the most heavily 
		populated country in the world. While the Manchu emperors adopted many aspects of Chinese 
		culture and political philosophy, they also actively strove to retain their native identity. 
		Traditional Chinese art and culture continued to flourish, however, often with imperial 
		sponsorship. Although Qing-dynasty officials practiced Tibetan Buddhism, they sponsored Taoist 
		rituals and maintained a Taoist temple in Beijing.
