<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Silk Roads&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		the long and arduous routes by which traders, missionaries, and others traveled between China and the 
		ancient Middle East, so named because silk traveled to the Mediterranean along these routes. The Silk 
		Roads stretched across northwest China into central Asia and then southward to what is now Afghanistan, 
		Pakistan, and India, and finally westward toward the Middle East and the Mediterranean.  The Silk Roads 
		were active as early as the Han dynasty and thrived through the cosmopolitan Tang dynasty.  Buddhism 
		made its way from India to China along these routes (as well as the Southern Sea route).
