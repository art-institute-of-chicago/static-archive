<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Complete Realization sect&nbsp;&nbsp;</B></FONT>	<I>(n)&nbsp;&nbsp;</I> 
		a Taoist monastic order founded in northern China around 1160. The sect combines the teachings 
		of Taoism, Buddhism, and Confucianism. The goal of the sect's followers was to attain immortality 
		by perfectly realizing the Tao in themselves. Both male and female members of this sect practiced 
		a strict monastic lifestyle. It survives today as one of the two major sects of Taoism, and its 
		headquarters is the White Cloud Monastery in Beijing.
