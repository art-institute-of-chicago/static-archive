<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Eight Immortals&nbsp;&nbsp;</B></FONT> 
		a group of legendary, semi-historical figures important in both religious 
		Taoism and popular religion. Artistic representations of each are usually recognizable 
		by identifying their respective attributes. The names of the Eight Immortals are Zhongli 
		Quan, He Xiangu, Zhang Guo, Lü Dongbin, Han Xiangxi, Cao Guojiu, Li Tieguai, and Lan Caihe. 
		Stories of the Eight Immortals were popularized in folklore, drama, novels, and woodblock prints.
