<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>religious Taoism&nbsp;&nbsp;</B></FONT>	<I>(n)</I> 
		a term used to define Taoism as an organized, institutionalized religion as opposed to the 
		original philosophical tradition. Religious Taoism developed between the second and fifth 
		centuries A.D. and built on the earlier philosophical foundations. Unlike philosophical 
		Taoism, religious Taoism incorporated new ritual practices and religious institutions, 
		established a priesthood, defined the Taoist Canon, and created a pantheon of deities.
