<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Five Sacred Peaks&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		five sacred mountains located along the five directions (north, south, east, west, and center) 
		that occupy powerful places in Taoist geography. The sacred mountains are not actually single 
		peaks; rather they are networks of peaks, cliffs, gorges, hills, ravines, etc. To communicate 
		with the deities on these mountains, emperors ordered the construction of important Taoist temples 
		on each peak. Taoists also believe that immortals inhabit the Five Sacred Peaks. On their slopes 
		grow the magical mushrooms that bestow immortality (see map).
