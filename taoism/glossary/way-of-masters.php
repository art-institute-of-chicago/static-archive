<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Way of the Celestial Masters&nbsp;&nbsp;</B></FONT> 
		the first formal Taoist religious organization, founded in the late Han dynasty by Taoist master 
		Zhang Daoling, who claimed to have received teachings from the deified Laozi. Members of the 
		Celestial Masters sect addressed the spiritual needs of the community. Communal rites were performed 
		regularly, especially during seasonal changes. The Celestial Masters sect was also responsible 
		for healing, which required the recording of misdeeds on a paper addressed to one of the Three 
		Officials (heaven, earth, or water). The movement remains active in China to this day.
