<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Song dynasty (960–1279)&nbsp;&nbsp;</B></FONT> 
		period of reunification after the disruptive Five Dynasties period (906–960). Not a strong 
		military power, the Song turned to maritime trade. The publication of handbooks and encyclopedias 
		promoted the widespread dissemination of information. Mining, metal-casting, and industrial mass 
		production reached a high level as water and road transportation improved. During the Song dynasty, 
		"Neo-Confucianism," a reinvigoration of traditional Confucianism with Buddhist and Taoist ideas, 
		developed. In religious Taoism, Inner Alchemy rose in importance and popular deities were incorporated 
		into the tradition. Song emperors were among the greatest imperial patrons of the arts in Chinese 
		history. Likewise, members of the civil-service bureaucracy, educated in literary, historical, and 
		artistic traditions, shaped a new and potent artistic taste that continues to affect Chinese art today.
