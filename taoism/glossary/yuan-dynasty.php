<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Yuan dynasty (1260–1368)&nbsp;&nbsp;</B></FONT> 
		a period of foreign occupation by the nomadic tribes of Mongolia. Europe's diplomatic and religious interest 
		in China grew during the Yuan dynasty, and missionaries arrived for the first time. Marco Polo 
		of Venice worked for 17 years in the service of Khubilai Khan, grandson of the Mongol conqueror 
		Genghis Khan. Faced with discrimination by foreign rulers, the educated Chinese recalled their 
		ast and turned their energies to art and culture, including theater (which was influenced by 
		Taoism), painting, and poetry. The Taoist Eight Immortals became popular, and the great Taoist 
		temple with its extraordinary painted murals, the Palace of Eternal Joy (Yongle Gong), was built.
