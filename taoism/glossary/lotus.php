<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Lotus&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a plant of the water-lily family that grows in water or mud. Because its blossoms emerge pure 
		and beautiful out of muddy waters, Buddhists view the lotus as a symbol of human beings' true 
		nature, which can remain unstained by the mud of the world. The lotus may also symbolize the 
		soul that has attained enlightenment, freed from the mire of the everyday world. The many seeds 
		of the lotus make it also a symbol of fertility. Although it was borrowed from Buddhism, the 
		lotus appears in many Taoist religious images.
