<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>seal&nbsp;&nbsp;</B></FONT>  
	<I>(n)&nbsp;&nbsp;</I> 
		an impression in the form of an emblem stamped on a document, painting, or piece of calligraphy 
		to document authorship, ownership, or general appreciation. Seals and inscriptions might also 
		be added to a work over the course of centuries, as the work passes from collector to collector; 
		thus, the study of seals can reveal the history of a work. Seal carving was considered to be a 
		gentleman's pastime in China, and many modern Chinese artists still carve their own seals. The 
		emblems themselves may be carved in stone or ivory. Impressions are always made in red ink.
