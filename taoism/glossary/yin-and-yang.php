<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>yin and yang&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		two opposing types of energy or contrasting forces. Yin is described as yielding, passive, negative, 
		dark, and female. Yang is dynamic, assertive, positive, light, and male. The two energies are 
		opposite and yet mutually dependent. Yin may become yang and vice versa, just as day becomes night, 
		cold becomes hot, and the reverse. The behavior of yin and yang describes the structure of any 
		event or thing. It may be said that their dynamic relationship describes the operation of the 
		Tao in its cycles of creation, and that their alternating movement underlies the structure of 
		everything in the universe. The concept of yin and yang is conveyed by the tiger and dragon and 
		by the Taiji symbol (see also Taiji diagram).
