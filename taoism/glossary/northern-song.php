<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Northern Song (960–1126)&nbsp;&nbsp;</B></FONT> 
		the early Song-dynasty period, during which the capital, Bianjing (modern Kaifeng), was located 
		along the Yellow River. A period of political reunification, the Northern Song was characterized 
		by strong sea trade rather than military power. The dynasty also brought improvements in river, 
		canal, and road transportation. The Northern Song emperors were great patrons of Taoism and the 
		arts. They were avid collectors and generous supporters of a painting academy. The Song emperor 
		Huizong (r. 1110–1125) believed he was an incarnation of a Taoist god.
