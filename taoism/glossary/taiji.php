<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Taiji&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		the yin-yang symbol or diagram that depicts a great, eternally turning circle (the Tao) in 
		which the complementary energies of yin and yang turn about each other. Within this cycle 
		of alternating energy all things can be produced. The familiar Taiji symbol did not come 
		into existence until the Song dynasty (see Taiji diagram).
