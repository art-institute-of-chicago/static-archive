<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Zhang Daoling&nbsp;&nbsp;</B></FONT> 
		(also known as Celestial Master Zhang) Taoist leader, from the second century A.D., who 
		converted the philosophical ideas of Taoism into a popular religion. He is said to have written 
		approximately 24 works of Taoist scripture, cured the sick through incantation, and taught people 
		to confess their wrong deeds. The image of Zhang Daoling riding a tiger became popular for 
		expelling insects, curing diseases, and protecting the family from calamity.
