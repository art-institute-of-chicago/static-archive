<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Qin dynasty (221–207 B.C.)&nbsp;&nbsp;</B></FONT> 
		dynasty in which China was unified under the first emperor, Qin Shi Huang Di. He established 
		central rule, standardized weights, measures, coins, and the writing system.  Some claim that 
		it was his idea to create the Great Wall in 214 B.C. to protect China from invasion.  He was 
		a particular believer in the notion of immortality and is famous for his terracotta army of 
		over 7,000 life-size pottery soldiers, found buried near his tomb in the early 1970s.
