<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Ming dynasty (1368–1644)&nbsp;&nbsp;</B></FONT>  
		the period following the Yuan dynasty in which native rule was restored. 
		Ming emperors and empresses sponsored the renovation of Taoist sacred sites and the practice 
		of reformed Taoist rituals. In the Ming dynasty, the Taoist god Zhenwu became a national protector. 
		Paintings characterized by great energy and vigor flourished during this time, and the porcelain 
		industry received major imperial patronage. Early Ming energy was followed by a powerful conservative 
		movement, echoed in the efforts to isolate China from the outside world. Nevertheless, the Portuguese 
		landed in China in 1514. In the 17th century, trade with the Netherlands began and Jesuit priests 
		entered southern China. Corruption in the late-Ming imperial court ultimately led to another foreign 
		occupation.
