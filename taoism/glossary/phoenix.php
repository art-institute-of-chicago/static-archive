<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>phoenix&nbsp;&nbsp;</B></FONT>	<I>(n)&nbsp;&nbsp;</I> 
			according to Taoist tradition, a mythical bird not related to the phoenix of Western 
			mythology, which arises from ashes. The Chinese phoenix is often paired with the dragon. 
			During the Ming and Qing dynasties, the phoenix symbolized the empress and the dragon 
			symbolized the emperor. According to ancient Chinese lore, the appearance of the phoenix 
			on the wutong tree was a testament to the peaceful rule of a virtuous emperor.
