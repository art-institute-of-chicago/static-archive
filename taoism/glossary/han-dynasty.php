<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Han dynasty (206 B.C.–A.D. 220)&nbsp;&nbsp;</B></FONT> 
		the period of great cultural and territorial expansion that brought China into contact with 
		other cultures, including those of Southeast Asia and Korea. During this time, Confucianism began 
		to flourish and Buddhism was brought to China by merchants along the Silk Roads. In the Han 
		dynasty, the civil service was developed, the first history of China was written, and the first 
		Chinese dictionary was compiled. Many concepts that became the foundations for religious Taoism 
		(e.g., notions of the cosmos and immortality) developed during this period, and Laozi was deified. 
		Taoist-inspired rebellions ultimately brought about the collapse of the dynasty. Many of the art 
		objects that survive from this period are burial objects that reflect ideas about cosmology and 
		the afterlife.
