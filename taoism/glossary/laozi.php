<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>Laozi&nbsp;&nbsp;</B></FONT> 
		literally, "old master," traditionally assumed to have been born in the sixth century B.C. He 
		is considered the author of the earliest Taoist philosophical text, the <I>		Classic of the Way and Its Power (Daode jing).</I> Historians now agree 
		that Laozi was a legendary figure developed to provide an author for the <I>		Daode jing</I>, which was compiled by a group of scholars in the third 
		century B.C. During the Han dynasty, Laozi was deified; he remains one of the most important 
		deities in religious Taoism.
