<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>hanging scroll&nbsp;&nbsp;</B></FONT> 
	<I>(n)&nbsp;&nbsp;</I> 
		a painting or piece of calligraphy made in the form of a vertical scroll hung either on a wall 
		or from the end of an attendant's pole. Unlike oil canvases or panels, scroll paintings could be 
		easily taken down and replaced at various times of the year to suit the tastes of visitors or to 
		mark certain occasions, such as the changing seasons. Scroll paintings are remounted every few 
		decades to repair any damage and help preserve the image (see scroll diagram).
