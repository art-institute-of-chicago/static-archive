<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>	<FONT COLOR=#807040><B>trigrams (Eight Trigrams)&nbsp;&nbsp;</B></FONT> 
	<I>(n, pl)&nbsp;&nbsp;</I> 
		symbols of the cycle of yin and yang energy present in all things. Each of the Eight Trigrams 
		consists of three horizontal lines that represent either yin or yang energy. Yang energy is 
		depicted as a continuous line, and yin energy by a broken line. Each of the trigrams embodies 
		a particular configuration of yin and yang, ranging from completely yang, with three unbroken 
		lines, to completely yin, with three broken lines. The Eight Trigrams can appear in different 
		orders, arranged in a circle (see trigram diagram). Not specific to Taoism, the Eight Trigrams 
		were absorbed into Taoism as it became an organized religion.
