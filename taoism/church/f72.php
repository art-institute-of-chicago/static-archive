<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Taoist Official of Earth</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Together with the previous and following painting (<I><a href=javascript:launchWin('/taoism/church/f71.php')>Taoist Official of Heaven</a></I> and <I><a href=javascript:launchWin('/taoism/church/f73.php')>Taoist Official of Water</a></I>), this painting is part of a <A HREF="javascript:launchHelp('/taoism/glossary/triptych.php', 'width=250,height=275')">triptych</a> depicting the <A HREF="javascript:launchHelp('/taoism/glossary/three-officials.php', 'width=250,height=275')">Three Officials</a>. It shows the Official of Earth traveling through a mountainous landscape, about to cross a bridge. It was the duty of the Three Officials to travel through the world observing and recording the good and bad deeds of people and then to punish them appropriately.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The Official of Earth is shown here on such an inspection tour. He is accompanied by martial figures appropriate to the severity of his office and  demons responsible for punishing wrongdoers. Of particular interest is the tree spirit, a small dark figure with barklike skin in the lower right corner of the painting. Watching over the demons, in the bottom center of the painting, is Zhong Kui, the "Demon Queller"—a fierce popular figure who was believed to have taken a vow to protect people from unfair demon attacks. His presence is most necessary here, since demons were known to be overly enthusiastic in their punishments, often indiscriminately harming innocent and guilty alike. This is one of the earliest known depictions of Zhong Kui, who would become a popular figure in later Chinese art.</P>
