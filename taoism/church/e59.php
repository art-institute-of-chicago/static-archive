<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Taoist Ritual Sword</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Swords were used in Taoist rituals to purify the sacred altar of negative energies. As such, they were primarily tools of exorcism. Swords were symbolic rather than real weapons and were probably never actually sharpened. However, this sword was constructed and balanced in exactly the same way as a combat sword, and the ritual dances in which it was used resembled forms of martial arts.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>One side of the blade bears an inscription modeled after that on a sword given to Emperor Xuanzong (r. 712–756) of the <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang dynasty</a> approximately one thousand years earlier. On the other side of the blade are inlaid images of important constellations in the Taoist heavens: the <A HREF="javascript:launchHelp('/taoism/glossary/lunar-mansions.php', 'width=250,height=275')">28 Lunar Mansions</a> and the Northern Dipper (Big Dipper). Taoists believed that the sword contained the energies of these constellations, especially the Northern Dipper, which was a powerful symbol of exorcism. The scabbard is decorated with the scaly skin of a ray dyed mineral green. Upon it are two dragons, symbols of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yang</a> energy.</P>
