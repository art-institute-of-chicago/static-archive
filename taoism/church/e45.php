<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Taoist Ritual at the Imperial Court</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This <A HREF="javascript:launchHelp('/taoism/glossary/hanging-scroll.php', 'width=250,height=275')">hanging scroll</a> depicts a Taoist ritual at the court of the <A HREF="javascript:launchHelp('/taoism/glossary/qing.php', 'width=250,height=275')">Qing dynasty</a> in <a href=javascript:launchWin('/taoism/map.php')>Beijing</a>. On the altar, an aristocrat kneels beside the Taoist priest leading the ceremony, who is shown under an umbrella. Four additional priests of lower rank stand to the sides. Below the altar, there is an orchestra of chimes, bells, and wind instruments playing music, an essential part of many Taoist rituals.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The most distinctive feature of this ritual is the altar, made from three levels of stacked tables. Taoist altars were usually made of pressed earth. These traditional earthen altars resemble the one shown here in that they were often temporary structures that would be dismantled after the ritual. </P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The ritual depicted here was probably a repentance (<I>zhai</I>) ceremony. In such a ceremony, the participants would confess their faults and perform different acts of <A HREF="javascript:launchHelp('/taoism/glossary/contrition.php', 'width=250,height=275')">contrition</a>.
</P>
