<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Ordination Scroll of Empress Zhang</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This scroll documents the <A HREF="javascript:launchHelp('/taoism/glossary/ordination.php', 'width=250,height=275')">ordination</a> of a <A HREF="javascript:launchHelp('/taoism/glossary/ming-dynasty.php', 'width=250,height=275')">Ming-dynasty</a> empress as a Taoist priest. It is one of the most important surviving documents of the relationship between Taoism and the Ming imperial family. The complete painting shows the empress Zhang (not seen in this detail), wife of the Hongzhi emperor, with a group of divine ladies called "jade maidens," the Taoist priest who ordained her, and a procession of deities. Each deity can be identified by an accompanying inscription, making this work an invaluable source for the identification of images of Taoist gods in the Ming dynasty. The depiction of empress and priest together with Taoist gods indicates that the human figures have achieved divine status.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>This detail shows a female deity followed by four male deities. All but one of the gods wears the robes of a Taoist priest and a cap topped with the symbolic flame of spiritual enlightenment. The fourth figure from the right wears the robes and cap of a scholar. All carry ivory <A HREF="javascript:launchHelp('/taoism/glossary/tablet.php', 'width=250,height=275')">tablets</a> modeled after those held by officials during meetings with the emperor, indicating that they are taking part in a similar <A HREF="javascript:launchHelp('/taoism/glossary/celestial.php', 'width=250,height=275')">celestial</a> audience. The background of clouds suggests that the ceremony is taking place in the heavens. Five-clawed dragons against a yellow background decorate the upper and lower borders: in this context, both the dragons and the color yellow symbolize the emperor.
</P>
