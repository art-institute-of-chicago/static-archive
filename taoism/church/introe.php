<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Despite its associations with individual spiritual pursuits, Taoism has always been a religion of nation, community, and family. Ritual was the primary expression of the religious needs and hopes of the community, and most Taoist rituals were developed in response to these needs. Rituals were performed for individuals at important transitional periods in their lives, such as birth and death. The significance of such moments, however, deeply affected their families.  In the case of the imperial house, such moments affected the entire nation.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Taoist priests used a number of sacred implements during rituals, each of which held symbolic significance. Smoke from incense burners both attracted the gods and carried messages from the community to the heavens. Swords were used for exorcism and purification ceremonies, and robes worn by the priest symbolized his power over the energies of the cosmos. Scriptures and other religious documents were also considered sacred and worshiped as embodiments of the <A HREF="javascript:launchHelp('/taoism/glossary/tao.php', 'width=250,height=275')">Tao</a>. Among the most magnificent works of Taoist art to be preserved is the <I><a href=javascript:launchWin('/taoism/church/e66.php')>Ordination Scroll of Empress Zhang</a></I>.</P>
