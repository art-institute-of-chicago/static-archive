<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>In the second century A.D., as the <A HREF="javascript:launchHelp('/taoism/glossary/han-dynasty.php', 'width=250,height=275')">Han dynasty</a> gradually lost control of China, large-scale religious movements sprang up all over the country. Between the second and sixth centuries, <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a> developed out of these movements, coming into maturity in the <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang dynasty</a>. The most important of these early movements was the <A HREF="javascript:launchHelp('/taoism/glossary/way-of-masters.php', 'width=250,height=275')">Way of the Celestial Masters</a>, which began in <A HREF="javascript:launchHelp('/taoism/glossary/sichuan.php', 'width=250,height=275')">Sichuan</A> province. <A HREF="javascript:launchHelp('/taoism/glossary/zhang-daoling.php', 'width=250,height=275')">Zhang Daoling</a>, founder of the Way of the Celestial Masters, supposedly received divine revelations from <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a> himself. Following this experience, Zhang developed a distinct system of religious beliefs based on the <I><A HREF="javascript:launchHelp('/taoism/glossary/classic.php', 'width=250,height=275')">Classic of the Way and Its Power</a>.</I></P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>In the centuries after the fall of the Han dynasty, China was often fragmented politically. Non-Chinese rulers eventually took control of the north, while Chinese rulers still governed the south. This was a fruitful period for both <A HREF="javascript:launchHelp('/taoism/glossary/buddhism.php', 'width=250,height=275')">Buddhism</a> and religious Taoism, which developed in response to the increasing spiritual needs of the people during this unstable time. The beliefs that developed during this period would form the core of the Taoist worldview.</P>
