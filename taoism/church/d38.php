<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Liberating the Soul from the Netherworld</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The large central scene of this <A HREF="javascript:launchHelp('/taoism/glossary/handscroll.php', 'width=250,height=325')">handscroll</a> shows a Taoist priest, kneeling on the lower left, in the presence of a powerful deity, the large central figure with a halo known as the <A HREF="javascript:launchHelp('/taoism/glossary/celestial.php', 'width=250,height=275')">Celestial</a> Worthy Who Saves the Suffering. Here, the priest requests the god's assistance on behalf of the soul of his <A HREF="javascript:launchHelp('/taoism/glossary/patron.php', 'width=250,height=275')">patron</a>. The scroll illustrates one of the most fundamental beliefs of Taoism: the role of the Taoist priest as a bridge between the human and divine worlds.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The right and left sides of the painting depict the good deeds of the patron. Those on the right take place in a Taoist temple: in the upper scene, the patron donates money to make Taoist objects of worship; in the middle scene, he sponsors a vegetarian meal for the priests of the temple; and in the lower scene, he participates in a ritual for his ancestors. These scenes correspond thematically to those on the left, which take place in the <A HREF="javascript:launchHelp('/taoism/glossary/secular.php', 'width=250,height=275')">secular</a> world: in the upper scene, the patron gives alms to a beggar; and in the middle scene, he commands the release of birds. The title of the scroll is based on the lower left scene, which shows the god descending into hell to liberate the soul of the patron because of the patron's good deeds and the priest's intervention. The inhabitants of hell bow in awed reverence to the god.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The signature of the artist, Liang Kai, indicates that this handscroll was commissioned by the emperor. A rare and important work by one of China's most innovative painters, this handscroll is also one of the finest examples of Taoist painting from the <A HREF="javascript:launchHelp('/taoism/glossary/southern.php', 'width=250,height=275')">Southern Song dynasty</a>.</P>
