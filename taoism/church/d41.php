<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Deified Laozi</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>After a period of rapid growth and consolidation in the second to sixth centuries A.D., <A HREF="javascript:launchHelp('/taoism/glossary/religious-taoism.php', 'width=250,height=275')">religious Taoism</a> reached its first peak when China was reunited by the <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang dynasty</a>. Based on the fact that they had the same family name (Li), the Tang imperial family claimed to be descended from <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a>, whose full name was Li Dan. In the Tang dynasty, Taoism became a national religion and was seen as both a means of spiritual fulfillment and a tool to strengthen the emperor's political power. The imperial family sponsored the establishment of Taoist temples all over the country, and Taoist priests played an important role at court.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Laozi is here depicted as both a perfected god at the top of the Taoist <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a> and a royal ancestor who represents the divinity of the ruling family. The balanced proportions of the statue suggest Laozi to be an ideal being free of human weakness or imperfection. This stands in stark contrast to the <I><a href=javascript:launchWin('/taoism/tradition/a1.php')>Portrait of Laozi</a></I> by Muqi.</P>
