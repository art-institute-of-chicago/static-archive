<CENTER><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco>Incense Burner with Li Tieguai</FONT></P></CENTER>

<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The incense burner is the most important object in a Taoist ritual. The smoke from the incense purifies the ritual space, alerts the gods that a ritual is about to be performed, and draws the gods to the altar. Incense smoke is also believed to resemble the swirling patterns of cloudlike energies underlying all matter. As such, the smoke infuses both the altar and the body of the priest with sacred energy. In this incense burner, the vents that allow the smoke to escape are in the shape of four <A HREF="javascript:launchHelp('/taoism/glossary/trigrams.php', 'width=250,height=275')">trigrams</a> from the <I>Book of Changes,</I> symbolizing the interactions of <A HREF="javascript:launchHelp('/taoism/glossary/yin-and-yang.php', 'width=250,height=275')">yin and yang</a>.</P> 
<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The incense burner is topped with an image of Li Tieguai, "Iron-Crutch Li," one of the <A HREF="javascript:launchHelp('/taoism/glossary/eight-immortals.php', 'width=250,height=275')">Eight Immortals</a>, important deities of the <A HREF="javascript:launchHelp('/taoism/glossary/complete.php', 'width=250,height=275')">Complete Realization sect</a> of Taoism. Legend has it that Li's soul was able to leave his body and travel to other places. Once, Li ordered a student to watch his body while his soul visited <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a>, instructing that student to burn his body if his soul did not return in seven days. On the sixth day, the student had a family emergency, so he burned Li's body and left a day early. When Li returned and found his body in ashes, he was forced to take the nearest body—that of a crippled beggar who had recently died. He had this hideous form forever after and was forced to rely on an iron crutch to walk.</P> 

