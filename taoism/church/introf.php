<P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>Broadly speaking, there are two kinds of deities in Taoism. The first formed spontaneously out of the <A HREF="javascript:launchHelp('/taoism/glossary/primordial.php', 'width=250,height=275')">primordial</a> energies at the beginning of the world. These are the highest gods of Taoism who hold titles like "Celestial Worthy" or "Emperor." These gods hold court in <A HREF="javascript:launchHelp('/taoism/glossary/celestial.php', 'width=250,height=275')">celestial</a> paradises and govern a complex <A HREF="javascript:launchHelp('/taoism/glossary/hierarchy.php', 'width=250,height=275')">hierarchy</A> of lesser gods similar to the hierarchy of emperor and officials on earth. Many of the most fundamental gods of this category developed between the second and sixth centuries A.D. and were consolidated into a well-defined <A HREF="javascript:launchHelp('/taoism/glossary/pantheon.php', 'width=250,height=275')">pantheon</a> by the <A HREF="javascript:launchHelp('/taoism/glossary/tang-dynasty.php', 'width=250,height=275')">Tang dynasty</a>. Among them are the <A HREF="javascript:launchHelp('/taoism/glossary/three-purities.php', 'width=250,height=275')">Three Purities</a> (including <A HREF="javascript:launchHelp('/taoism/glossary/laozi.php', 'width=250,height=275')">Laozi</a>) and the <A HREF="javascript:launchHelp('/taoism/glossary/three-officials.php', 'width=250,height=275')">Three Officials</a>. As pure embodiments of the Way, these Taoist gods are abstract figures defined more by their rank than by their personal mythology. They do not accept sacrifices of food or alcohol and can only be contacted through official requests written by Taoist priests.</P><P><FONT FACE=Verdana,Arial,Helvetica,Helv,Monaco SIZE=-1>The second category of Taoist deities consists of human beings who—through learning, self-discipline, <A HREF="javascript:launchHelp('/taoism/glossary/alchemy.php', 'width=250,height=275')">alchemy</a>, or other means—have purified themselves of mortal imperfections and become gods, a transformation often described as "ascending to the heavens in broad daylight."</P>
